									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> 
									Tuberculin टेस्ट में यदि Reactionary bleb 5 mm से कम हो तो उसे negative मानते हैं, 5 से 10 mm तक indeterminate और 10 mm से अधिक को positive मानते हैं।</p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> संक्रमित और बलगम में पॉजिटिव TB के मरीज के सभी परिवार जन को बच्चे जो 5 साल की उम्र से कम हैं।</p>

									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> HIV के मरीज जो एक वर्ष से अधिक आयु के हैं।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Infant जो HIV संक्रमित हों और TB k मरीजों के संपर्क में आए हों।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Silicosis, Dialysis के मरीजों को।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> जो मरीज Organ या hematological ट्रांसप्लांट के लिए सुनिश्चित हों या Anti TNF के इलाज पर हों।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> वो लोग जो भीड़ भाड़ वाले माहौल में रह रहे हों, काम कर रहे हों या ऐसे देश या इलाके से आएं हों जहां TB की तादात अधिक हो।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> IV Drug लेने वाले लोग।</p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> TB preventive treatment में Isoniazid 5-15 mg/kg/day, Rifampin 15-30 mg/kg/day ये दो दवाइयां दी जाती हैं।</p>
									
									<p class="mb-2"><b>Management:</b> </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> यदि मरीज ने पहले कभी इलाज लिया है तो HRZES रेजिमेन लेता है।</p>
									<p class="mb-2">H Isoniazid 75 mg</p>
									<p class="mb-2">R Rifampicin 150 mg</p>
									<p class="mb-2">E Ethambutol 275 mg</p>
									<p class="mb-2">Z Pyrazinamide 400 mg</p>
									<p class="mb-2">S Streptomycin 1000 mg</p>

									
									<p class="mb-2"><b>New drugs in TB regimen</b> </p>									
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Bedaquiline BDQ यह दावा Mycobacterial ATP Synthase receptor की Inhibitor है और इसका dose 400 mg/day 2 हफ्तों तक उसके बाद 200 mg/day हफ्ते में तीन बार 22 हफ्तों तक है ।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Delamanid DLM यह दावा Mycolic Acid Synthesis की अवरोधक है। इसकी dose 6-11 वर्ष के बच्चों में 50mg दिन में दो बार 24 हफ्तों तक है। तो वहीं 12 वर्ष और उसके ऊपर के लोगों में यह dose 100mg दिन में दो बार 24 हफ्तों तक है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> 5 वर्ष से ऊपर के MDR तथा RR-TB में यह दावा दी जाती है। Bdq तो 18 वर्ष से ऊपर और Dlm 6 वर्ष के ऊपर इंगित है। </p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> गर्भावस्था में, स्तनपान में, OC Pills लेने वाली महिलाओं में और अनियंत्रित cardiac arrhythmia, Torsades De Pointes, heart failure और Hypokalemia में यह नई TB की दवाइयां नहीं देनी चाहिए ।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> गर्भावस्था में TB की ATT 2 महीने HRE और 7 महीने HR चलाई जाती है। स्तनपान को प्रोत्साहित किया जाता है। मां और बच्चे दोनो को 10 mg/day और 5 mg/day क्रमशः Pyridoxine दिया जाता है। </p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> यदि TB Positive मां का बच्चा TB संक्रमित नही है, तो भी Isoniazid prophylaxis बच्चे को दी जाती है ।</p>

									<p class="mb-2"><b>भिन्न भिन्न तरह की T.B. को drug sensitivity के हिसाब से बांटा गया है।</b> </p>	
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Mono Drug resistant TB में केवल Isoniazid का resistance होता है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Polydrug resistance में Isoniazid और एक प्रथम श्रेणी दवा ethambutol या Pyrazinamide का resistance होता है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> MDR TB में rifampin और Isoniazid इन दोनो दवाइयों से resistance होता है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> MDR TB में rifampin और Isoniazid इन दोनो दवाइयों से resistance होता है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> जांचों में Genexpert या CBNAAT भी कहते हैं एक बहुत्व महत्त्व पूर्ण जांच हैं। यह जांच Cartridge based Nucleic Acid Amplification Test कहलती है। इस में Nucleic Acid की मात्रा बढ़ाई जाती है, यह मात्रा बढ़ जाने पर इसे परखना सरल हो जाता है। </p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> यह जांच 85% sensitivity और 98% specificity के साथ  2 घंटे से भी कम के समय में रिपोर्ट दे सकती है और साथ ही 96% sensitivity और 98%specificity के साथ rifampicin resistance की भी रिपोर्ट दे देती है। </p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Rifampicin resistance वाले लगभग  सभी बैक्टीरिया Isoniazid को भी resistance होते हैं। इसीलिए WHO ने GeneXpert इस जांच को investigation of Choice रखते विश्व भर में   इसी जांच पर जोर दिया है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Quantiferon गोल्ड टेस्ट यह टेस्ट memory T helper cells CD4+  को परखता है। उसी तरह Quantiferon गोल्ड प्लस टेस्ट सीडी8+ और CD 4+ दोनो को परखता है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Tuberculin sensitivity test या Mantoux test ना तो sensitive है ना ही specific है। यह टेस्ट TB के संक्रमण और TB के एक्टिव बीमारी में भेद नहीं कर पाता है। इसलिए यह पुराना टेस्ट अब उपयोग और इलाज से बाहर है।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> विश्व स्वास्थ संघ ने CBNAAT, Liquid Cell Culture, Line Probe assay (extrapulmonary TB के लिए), LED/Conventional Microscopy को मान्यता दी है।</p>
											
									<p class="mt-3"><b>Presumptive TB</b> - यदि किसी को बुखार , खांसी, रात में पसीने आना और वजन कम होना यह सारे लक्षण है तो यह मान लिया जाता है की उसे TB हो सकती है।</p>		
									<p><b>Recurrent TB</b> - यदि किसी को एक बार पूरी तरह इलाज लेने के बाद दुबारा बलगम में TB के बैक्टीरिया पाए जातें हैं।</p>		
									<p> <b>Lost to follow up TB</b> - यदि किसी ने कम से कम 1 महीने तक TB की दवा खाई हो और छोड़ दिया हो तो उसे Defaulter कहते थे अब इन के लिए lost to follow up इस नामांकन से संबोधित करते है।</p>		
									<p class="mt-4"> <b>TB की दवाइयों को दो गुटों में वर्गीकृत किया गया है। </b></p>	

