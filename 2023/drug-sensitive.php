									<p class="mb-2"><b>Presumptive TB</b> - यदि किसी को लंबे समय से बुखार , खांसी, रात में पसीने आना और वजन कम होना यह सारे लक्षण है तो यह मान लिया जाता है की उसे TB हो सकती है।</p>
									<p class="mb-2"><b>Recurrent TB</b> - यदि किसी को एक बार पूरी तरह इलाज लेने के बाद दुबारा बलगम में TB के बैक्टीरिया पाए जातें हैं। </p>
									<p class="mb-4"><b>Lost to follow up TB </b>- यदि किसी ने कम से कम 1 महीने तक TB की दवा खाई हो और छोड़ दिया हो तो उसे Defaulter कहते थे अब इन के लिए lost to follow up इस नामांकन से संबोधित करते है।</p>

