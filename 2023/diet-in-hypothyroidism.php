									<p class="mb-2">दोपहर के भोजन में साबुत अनाज, सब्जियां, लीन प्रोटीन और फल शामिल होने चाहिए।</p>

									<p class="mb-2">दोपहर के भोजन के लिए brown rice, जौ, ज्वार और बाजरा के व्यंजन अच्छे विकल्प हैं।</p>

									<p class="mb-2">रात का खाना</p>

									<p class="mb-2">रात के खाने में सब्जियां, साबुत अनाज, बाजरा, समुद्री भोजन या lean protein, और फलों का कोई अन्य स्रोत शामिल होना चाहिए।</p>

									<p class="mb-2">रात के खाने के लिए, quinoa, wild rice और buckwheat  अनाज से बने भोजन अच्छे विकल्प हैं</p>

									<p class="mb-2">जो लोग भोजन के बीच में snacking का आनंद लेते हैं, उन्हें baked snacks , और dry fruits का सेवन करना चाहिए, और deep fry से बचना चाहिए।</p>

									<p class="mb-2">नोट: आहार की मात्रा उम्र, लिंग, शारीरिक गतिविधि और अन्य चिकित्सा स्थितियों की उपस्थिति पर निर्भर करती है।</p>

