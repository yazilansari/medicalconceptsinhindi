									<h5>Growth Hormone Receptors And Mechanism Of It's Action</h5>
									<p class="mb-3">Describes How Does Growth Hormone Acts On The Body</p>								
									<p class="mb-3"><b>Mechanism of GH action and Growth hormone receptor (GHR)</b></p>

									<p class="mb-3">किसी भी अन्य cell membrane receptor की ही भांति GHR के भी तीन भाग होते हैं, </p>

									<p class="dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Extracellular ligand binding domain - जो circulating GH (ligand) से combine करता है।</p>


									<p class="mb-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i>  
										<b>Transcellular domain</b> - जो ligand binding के signals cell के भीतर पहुंचता है, एवं</p>
									<p class="mb-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> 
									<b>Intracellular domain</b> </p>

									<p class="mb-3 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i>  
									<b>Intracellular domain</b> - जो cell के भीतर, विभिन्न kinases के द्वारा GH का action उत्पन्न करता है।</p>

									<p class="mb-4">GHR की 2 subunits एक साथ मिलकर किसी <b>dimer</b> के रूप में कार्य करती हैं। GH का एक molecule, GHR की इन दो subunits (dimer) के extracellular ligand binding domains से एक साथ combine करता है। इसके फलस्वरूप, GHR के intracellular domain में transformation उत्पन्न होता है जो 
										<b>Jenus kinase 2 (JAK-2)</b> को activate करता है। </p>
									

									<p class="mb-3 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i>  <b>Signal transducing activators of transcription proteins (STAT)</b> - Activated STAT, translocation के द्वारा nucleus में पहुंचकर वहां insulin like growth factor-1 (IGF-1) एवं अन्य new protein production के लिए transcription को बढ़ाता है जिससे GH के actions उत्पन्न हो सकें। </p>

									<p class="mb-3 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Insulin receptor substrate (IRS)</b> - वास्तव में GH एवं insulin में काफी अधिक साम्य भी मिलता है। GH के glucose metabolism से सम्बंधित प्रभाव, insulin signalling cascade से सम्बंधित IRS के activation के द्वारा संपन्न होते हैं। </p>

									<p class="mb-4 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> 
										<b>Mitogen activated protein kinase (MAPK)</b> - GH के cell proliferation से सम्बंधित प्रभाव MAPK के activation के द्वारा संपन्न होते हैं।  </p>
									
									<p class="mb-2 mt-5">शरीर की विभिन्न cells पर GH के प्रभाव दो प्रकार से संपन्न होते हैं।</p>

									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Direct action via GH receptors (GHR)</b> - GHR मुख्यतः liver, cartilage, muscles एवं adipose tissues पर मिलते हैं। </p>

									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Indirect action via insulin like growth factor-1 (IGF-1) and IGF-1R -</b> Liver में GH, GHR पर कार्य करते हुए तीन प्रकार की proteins उत्पन्न करते हैं। IGF-1, IGF binding protein-3 (IGFBP-3) एवं acid labile subunit (ALS) । यह तीनों परस्पर combine होकर एक complex का निर्माण करती हैं जो blood में circulate करता रहता है एवं IGF-1 के storehouse के रूप में कार्य करता है। इसी complex की proteolysis से IGF-1 release होकर GH के actions को mediate करता है। </p>

									<p class="mb-2 mt-5">शरीर की विभिन्न cells पर GH के प्रभाव दो प्रकार से संपन्न होते हैं।</p>
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Endocrine IGF-1</b> - Liver cells से एवं उपरोक्त complex की proteolysis से उत्पन्न IGF-1, blood में circulate करता हुआ, किसी hormone की ही भांति सम्पूर्ण शरीर में circulate करता है एवं IGF-1R के माध्यम से अपना प्रभाव उत्पन्न करता है। क्योंकि liver द्वारा उत्पन्न होने वाला यह IGF-1 एवं इसकी IGFBP-3 दोनों GH के प्रभाव में ही उत्पन्न होते हैं अतः GH के secretion एवं action को जानने के लिए इनका उपयोग किया जाता है। </p>			
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Paracrine एवं autocrine IGF-1 - IGF-1</b> की कुछ मात्रा, GH के द्वारा peripheral cells में भी उत्पन्न होती है। Cartilaginous growth plate पर locally उत्पन्न हुआ IGF-1, अपने autocrine एवं paracrine effects के द्वारा growth में महत्वपूर्ण योगदान देता है। IGF की दो अन्य binding proteins, IGFBP-2 एवं IGHBP-3, इस locally produced IGF के regulation में प्रयुक्त होती हैं।</p>									
