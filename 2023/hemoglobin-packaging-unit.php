									<h5>RBC - A hemoglobin packaging unit</h5>					
																		
									<p class="mb-3">Lower animals में haemoglobin, plasma में ही free form में मिलता है जबकि mammals में RBCs के भीतर। क्या तुम बता सकते हो कि ऐसा करने से क्या लाभ मिला? वास्तव में free hemoglobin, capillary membrane को permeate कर के बाहर निकल सकता है जिसके कारण plasma में इसकी concentration कम हो सकती है। यहाँ तक कि प्रत्येक circulation में 3% तक hemoglobin, blood से ECF में leak कर जाता है या kidneys द्वारा urine में filter हो जाता है। इस प्रकार हो सकने वाले hemoglobin के loss को रोकने के लिए ही mammals में hemoglobin की packaging RBCs में की गयी है।</p>
									
									<p class="mb-3">क्या hemoglobin की मात्रा एवं RBC के size के मध्य भी कोई सम्बन्ध हो सकता है? जरा सोचो, RBC का विकास hemoglobin की packaging के लिए ही तो हुआ है। RBC के development के साथ ही proerythroblast अथवा basophil erythroblast  की stage से हो hemoglobin का निर्माण आरम्भ हो जाता है। जैसे-जैसे red colored hemoglobin की मात्रा बढ़ती जाती है, यह प्रारंभिक basophilic (blue colored) erythroblasts, polychromatophillic (multicolored) होते हुए orthochromatic (single, red colored) होती जाती हैं। शायद hemoglobin  की अधिकतम मात्रा store कर सकने के प्रयास में ही RBC का nucleus भी छोटा होते-होते बिलकुल समाप्त हो जाता है। प्रत्येक RBC लगभग 34% तक ही hemoglobin अपने अंदर store कर पाती है, जिसका अर्थ हुआ, 34 gram hemoglobin प्रति 100 ml RBC । यही RBC की metabolic limit है। RBC का size बढ़ने पर भी प्रति RBC, hemoglobin की कुल मात्रा (mean corpuscular haemoglobin – <b>MCH</b>) तो बढ़ सकती है परन्तु प्रत्येक में hemoglobin concentration (mean corpuscular haemoglobin concentration – <b>MCHC</b>) 34 gram/100 ml से अधिक नहीं बढ़ सकती। इसीलिए, <b>macrocytic</b> RBCs तो मिलती हैं पर <b>hyperchromic</b> RBCs नहीं। यदि किसी कारण से hemoglobin के निर्माण में कमी आ रही हो तब ऐसी अवस्था में hemoglobin की कम मात्रा को store करने के लिए RBC को बड़े size की आवश्यकता ही नहीं रहेगी एवं वह सामान्य से छोटी (<b>microcytic</b>) एवं less pigmented (<b>hypochromic</b>) ही रह जाएंगी।</p>
									
									<p class="mb-3">जरा सोचो, एक सामान्य RBC एक narrow capillary से बड़ी आसानी से निकल जाती है जबकि <b>hereditary spherocytosis</b> में मिलने वाली spherocytes नहीं। इसका क्या कारण हो सकता है? ध्यान दो, एक सामान्य RBC की shape biconcave होती है। इस प्रकार RBCs में intracellular fluid की अपेक्षा उसकी cell membrane काफी अधिक होती है जो उसे कई प्रकार से मुड़ने या deform होने में रुकावट नहीं डालती। इस कारण से एक सामान्य RBC बड़ी सरलता से अपने आकार से छोटी capillary से होकर भी निकल जाती है। इसके विपरीत, <b>spherocytes</b> आकार में spherical होती हैं जिनमें intracellular fluid की अपेक्षा cell membrane काफी कम होती है। इसके कारण जब वह किसी छोटे स्थान से गुजरती हैं तब बाहर से पड़ने वाले pressure से इनका intracellular pressure अत्यधिक बढ़ जाता है जिससे वह rupture हो जाती हैं। </p>									
									<p class="mb-3">तुम जानते हो कि RBCs में nucleus नहीं होता। क्या तुम बता सकते हो कि इसके अतिरिक्त और भी कोई अन्य cell, non-nucleated होती है? याद करो, platelets भी तो non-nucleated cells हैं, परन्तु इन दोनों के मध्य क्या अंतर है, यह समझते हैं। RBC एक पूर्ण विकसित cell है जो आरम्भ में nucleated थी परन्तु विकास के साथ इसका nucleus लुप्त होता गया। इसके विपरीत, platelet पूर्ण cell न हो कर एक large cell, <b>megakaryocyte</b>, का एक fragment मात्र है। इसीलिए, RBC का अपनी एक निश्चित size एवं shape होती है, जबकि platelets की नहीं। Platelets अलग-अलग size के एवं irregular shapes के होते हैं। RBCs एवं platelets में एक अंतर यह भी होता है कि, platelets में mitochondria बचे रहते हैं जिससे उनमें ATP बनाने की क्षमता बची रहती है, जबकि RBCs में कोई भी cell orgenelle नहीं मिलता।</p>									
									
