									<h5>Water-fundamental basis of life intracellular and extracellular fluids</h5>					
																		
									<p class="mb-3">तुम यह तो जानते ही हो कि जीवन का आरंभ जल में हुआ था। इतने लंबे evolution के बाद भी यह तथ्य आधुनिक मनुष्य तक में सत्य है। ध्यान दो, fertilization, embryonic life, fetal development, इत्यादि सभी प्रक्रियाएं  liquid environment में ही होती हैं। वास्तव में संपूर्ण शरीर का basic medium liquid ही है, जो मानव शरीर का लगभग 60% भाग बनाता है। इसमें से अधिकांश (2/3) cells के भीतर, <b>ICF</b>, एवं शेष (1/3) cells के बाहर, <b>ECF</b>, के रुप में रहता है। कल्पना करो, यह ECF किसी विशाल समुद्र के समान ही तो है जिसमें विभिन्न cells बीच बीच में डूबी हुई हैं। इसी लिए ECF को शरीर का internal sea भी कहते हैं। वास्तव में ECF का composition सृष्टि के प्रारम्भ में रहे primordial sea के समान ही लगता है जो पुनः हमें इस तथ्य की याद दिलाता है कि, जीवन की उत्पत्ति जल में हुई थी।</p>
									
									<p class="mb-3">प्रत्येक cell के ICF का निर्धारण उसकी अपनी आवश्यकताओं के अनुसार होता है। इसके विपरीत, ECF का composition संपूर्ण शरीर में लगभग एक समान होता है कि जिसे शरीर का <b>internal environment</b> कहते हैं। यह concept वास्तव में संपूर्ण modern physiology का केन्द्र बिन्दु है, जिसे Claude Bernard ने <b>milieu interior</b> कहा और जिसके लिये उन्हें father of modern physiology भी कहा जाता है।</p>

									<p class="mb-3">जरा सोचो, सम्पूर्ण शरीर में मिलने वाले इस ECF का composition एक समान बनाये रखना कैसे संभव हो सकेगा? Multicellular micro-organisms में तो simple diffusion की प्रक्रिया द्वारा ही यह एकरूपता बनायी रखी जा सकती है परन्तु body size बढ़ने के साथ-साथ, simple diffusion मात्र से यह कार्य कठिन होता जाता है। Small organisms में भी body cavity में होने वाला fluid movement इसमें मदद कर सकता है परन्तु larger animals में इसके लिए एक अलग तंत्र का विकास करना पड़ा जो संपूर्ण शरीर में circulate कर सभी स्थानों के ECF को एक equilibrium में बनाये रखने में मदद कर सके। इस प्रकार यह blood संपूर्ण शरीर के ECF को परस्पर ही connect करता है, जिसके लिए इसे <b>connective tissue</b> कहा जाता है। Blood vessels में बहता blood, ECF को दो भागों में विभाजित कर देता है। प्रथम, <b>intravascular fluid</b> जिसे plasma कहते हैं तथा द्वितीय, <b>extravascular fluid</b>, जिसे cells के बीच बीच में होने के कारण <b>interstitial fluid (ISF)</b> कहते हैं।</p>

									<p class="mb-3">जरा सोचो, क्या केवल connected रहने मात्र से ही सम्पूर्ण शरीर के ECF का composition एक समान हो जायेगा? वास्तव में, इसके लिए तो circulating plasma एवं ISF को परस्पर संपर्क में रहना होगा। इसी लिए, circulatory system की capillary walls एक semipermeable membrane की भांति कार्य करती हैं जिससे इन दोनों fluids के मध्य substances का आवागमन सरलता पूर्वक हो सके। Blood द्वारा सम्पूर्ण शरीर के ECF (internal environment) को समान बनाये रखने की इस प्रक्रिया को ही Walter Cannon ने <b>homeostasis</b> कहा है। Plasma एवं ISF के मध्य जिन larger substances का आदान प्रदान capillary membrane के माध्यम से नहीं हो पाता, उसके लिए शरीर में एक और तंत्र का विकास हुआ जिसे lymphatic system कहते हैं। इस प्रकार यह lymph भी plasma एवं ISF के मध्य एक bridge का कार्य करता है।</p>

									<p class="mb-3">इस प्रकार, ICF एवं ECF में उनकी परस्पर आवश्यकताओं के अनुसार विभिन्न प्रकार के ions की मात्रा में कुछ कुछ भिन्नता होती है। ICF में K+, Mg++ एवं PO4– – – की मात्रा ECF से अधिक होती है एवं ECF में Na+, Ca++ एवं CI– की मात्रा ICF से अधिक होती है। यदि electrical activity के अनुसार देखें तो इन सब में Na+ एवं K+ सर्वाधिक महत्वपूर्ण हैं। ECF में Na+, ICF में Na+ की अपेक्षा 14 गुना अधिक मात्रा में मिलता है। (142 vs 10 mg/l)। इसके विपरीत ICF में K+, ECF में K+ की अपेक्षा 35 गुना अधिक मात्रा में होता है (140 vs 4 mg/l)। Na+ एवं K+ के स्तरों की यह भिन्नता ही cells की electrical activities को संचालित कराती है।</p>
