									<h5>DRUG SENSITIVE टी. बी.</h5>
									<p class="mb-4">डॉ श्वेता शर्मा सह आचार्या मेडिसिन, डॉ विवेक यादव जूनियर रेजिडेंट मेडिसिन</p>														
									<p class="mb-3">लाला लाजपतराय स्मारक वैदिकीय महाविद्यालय, मेरठ</p>

									<p class="mb-3"><b>इतिहास-:</b></p>									
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> यह एक अत्यंत प्राचीन बीमारी है । 3300 वर्ष पूर्व के भारतीय और 2300 वर्ष पूर्व के चीनी लेखों में इसके प्रमाण दर्ज हैं। </p> 
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसके सबसे पुराने प्रामाणिक पुरातत्व अवशेष 9000 वर्ष पूर्व इजराइल के हैं जहाँ इसके अंश मिलें हैं।</p> 
									

									<p class="mb-3"><b>भारत में TB का इतिहास:-</b></p>									
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> लगभग 2200 वर्ष पूर्व लिखे गए चरक संहिता के आठवें अध्याय चिकित्सा स्थान खंड  में इस बीमारी का राजयक्षमा नाम से उल्लेख किया गया है।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इस बीमारी के विषय में तीन पाठों में वर्णन किया गया है जिनके नाम राजयक्षम, सोष निदान, एवम क्षतक्षिण चिकित्सा है।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इन पाठों में यह बताया गया है की ये बीमारियां लगभग एक जैसी है जिनके लक्षण एक हैं शरीर का क्षय होना। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> महर्षि सुश्रुत ने इस बीमारी को contagious बताया है। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> चरक संहिता में इसकी etiology में क्षय अर्थात wasting का नाम लिया गया है। कदाचित यहीं से इसे इसका वर्तमान नाम क्षयरोग मिला है।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Clinical presentation में त्रिरूप, शदरूप एवम एकदशरूप इस तरह से इस बीमारी को mild, moderate और severe रूप का वर्णन किया है।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसी पाठ में महर्षि चरक ने रक्तस्थिवन नाम से Hemoptysis का उल्लेख किया है और यह बताया है की यह भी इस बीमारी का एक प्रमुख लक्षण है।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> जितनी पुरानी यह बीमारी है उतना ही रोचक इसके नाम, इसके किस्से और इससे जुडी सामाजिक डर, अन्धविश्वास भी रहे हैं जो समय के साथ ओझल हो गए। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> यह बीमारी उस दौर से मानव इतिहास को डराती आयी है जब यह माना जाता था की बीमारियां ईश्वर के प्रकोप हैं। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> 24 मार्च 1882 में Dr Robert Koch ने ऐसी कई सारी प्रचलित धारणाओं को  असत्य साबित करते हुए यह प्रमाण दिया कि यह बीमारी Mycobacterium नमक एक जीवाणु की वजह से होती है। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> वहीं से शुरू हुई इसके इलाज़ के शोध की प्रक्रिया आज के आधुनिक युग में इसे मिटाने की कोशिश कर रही है। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> सन 1905 में अनेको शोध और महान कार्यों के लिए Dr Robert Koch को नोबेल पुरस्कार से गौरवान्वित किया गया था। इन्ही के नाम से आज  tuberculosis को koch's disease भी कहते हैं।</p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> आज यही कारण है की 24 मार्च को विश्व टी. बी. दिवस के रूप में देखा जाता है और प्रतिवर्ष नई नीतियों के साथ इससे लड़ने के लिए विश्वभर की अनेको संस्थाएं पुरजोर प्रयास के साथ आगे बढ़ती हैं। </p>
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> विश्व स्वास्थ संगठन का इस 2022 वर्ष के टी. बी. दिवस का शीर्षक वाक्य था “Invest to end TB. Save Lives”</p>

									

