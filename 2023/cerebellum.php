									<h5>Cerebellum</h5>	
									<!-- <p>Spirometry - Method And Interpretation</p> -->
									
									<!-- <p class="mb-2"><b>Lung Function Tests (Part 1)</b></p> -->
									<p class="mb-3">Latin शब्द cerebellum का अर्थ होता है, little brain । यह नाम इसे cerebrum (जिसका अर्थ है, large brain) से अलग दिखाने के लिये दिया गया था, क्योंकि दिखने में cerebellum, cerebrum की अपेक्षा छोटा एवं कम महत्व का प्रतीत होता है। वास्तव में cerebellum का कुल वजन पूरे brain के वजन का 10% मात्र होता है।</p>
									
									<p class="mb-2">तुम यह जानते ही होगे कि cerebellum का मुख्य कार्य विभिन्न motor activities का coordination है। स्वाभाविक रूप से यदि किसी को अनेकों लोगों से coordinate करना है तब उसे लगातार उन सबके सम्पर्क में भी रहना होगा। इसीलिये cerebellum भी CNS के विभिन्न भागों से असंख्य neurons के माध्यम से जुड़ा रहता है। वास्तव में, brain में उपस्थित कुल neurons के आधे तो cerebellum में ही मिलते हैं।</p>

									<p class="mb-2">क्या तुम यह अनुमान लगा सकते हो कि इनमें से किन प्रकार के neurons की संख्या अधिक होती होगी? CNS के विभिन्न भागों से cerebellum में आने वाले neurons (cerebellar afferents) की अथवा cerebellum से अन्य भागों में जाने वाले neurons (cerebellar efferents) की। वास्तव में, cerebellar afferents, efferents की अपेक्षा 40 गुना तक अधिक होते हैं। यह तथ्य cerebellum की coordinating capacity का अनुमान भी दिलाता है।</p>									
									<p class="mb-2">जरा सोचो, cerebellum का आकार तो brain का 10% है, फिर brain के 50% से अधिक neurons इसमें किस प्रकार समा पायेंगे? इसीलिये cerebellum की सतह अनेक folds बना लेती है, जिससे इसका surface area बढ़ जाता है, जहां यह neurons समाहित हो सकें। यह surface folding, cerebellum के surface area को cerebrum के surface area के 75% तक बढ़ा देते हैं।</p>									
									
									<p class="mb-2 mt-3"><b>Morphology</b></p>									
									<p class="mb-2">बाहर से देखने पर तुम cerebellum को किसी गेंद के 3/4 भाग की तरह पाओगे, जिसकी तुलना 3/4 संतरे से भी की जा सकती है। यदि संतरे के पूरे छिलके को एक साथ फैलाकर सीधा करने का प्रयास करो तब वह उसी प्रकार दिखेगा, जैसा cerebellum के posterior view के चित्र में दिखाया जाता है। Posterior view में cerebellum के 2 स्पष्ट भाग दिखते</p>									
									<p class="mb-2 mt-3">निचला छोटा भाग – <b>Floculonodular</b><br> <b>lobe</b> या <b>vestibular cerebellum।</b> यह मूल रुप से एक balancing organ है जो evolution के आरंभ से ही aquatic vertebrates में विकसित होने लगा था। इसीलिये इसे archae (प्राचीन) <b>cerebellum</b> भी कहते हैं।</p>															
									<p class="mb-2 mt-3">मुख्य बड़ा भाग – <b>Corpus cerebellum</b> – यह भी एक transverse fissure द्वारा 2 lobes में विभाजित रहता है।</p>		

									<p class="mb-2 mt-3"> छोटा <b>anterior lobe</b> – जो मनुष्य की अपेक्षा lower mammals में अधिक विकसित रहता है। इसे <b>paleo</b> (पुराना) <b>cerebellum</b> भी कहते हैं।</p>		
									
									<p class="mb-2 mt-3"> बड़ा <b>posteiror lobe</b> – जो मनुष्यों में अर्थात् evolution में सबसे बाद में विकसित होने के कारण neo (नवीन) <b>cerebellum</b> भी कहलाता है।</p>	
									
									<p class="mb-2 mt-4"><u><b>Connections</b></u></p>		
									<p class="mb-2">Cerebellum 3 peduncles (stalk या डंठल) के माध्यम से मुख्य brain से जुड़ा रहता है, जिनके द्वारा सभी afferents एवं efferents का आवागमन होता है।</p>		
									
									<p class="mb-2"><b>Superior cerebellar peduncle</b> – इसे brain stem एवं thalamus से जोड़ता है।</p>																		
									<p class="mb-2"><b>Middle cerebellar peduncle</b> – इसे pons से जोड़ता है।</p>									
									<p class="mb-2"><b>Inferior cerebellar peduncle</b> – इसे brain stem एवं spinal cord से जोड़ता है।</p>	
