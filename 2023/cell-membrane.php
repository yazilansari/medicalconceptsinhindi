									<h5>Cell Membrane</h5>	
									<!-- <p>Spirometry - Method And Interpretation</p> -->
																		
									<p class="mb-2">Neurons अथवा यूं कहें कि शरीर में उपस्थित सभी cells में electrical charges के इस flow के लिये जो structure सर्वाधिक महत्वपूर्ण है, वह है cell membrane। यदि किसी cell में भीतर आने वाले एवं बाहर जाने वाले पदार्थों की मात्रा को नियंत्रित न किया जाये तब तो intracellular fluid (ICF) एवं extracellular fluid (ECF) का composition भी एक समान हो जायेगा। इससे तो किसी cell की अपनी विशिष्टता ही समाप्त हो जायेगी। इसीलिये cell membrane में एक अतिआवश्यक गुण विकसित होता है, जो है selective permeability। प्रत्येक cell अपनी आवश्यकताओं के अनुसार विभिन्न पदार्थों को अपने अंदर आने देती है एवं जो अनावश्यक हैं उन्हें बाहर निकालती रहती है। क्योंकि ICF एवं ECF दोनों water based systems हैं अतः सर्वप्रथम तो इनके मध्य water molecules के movement को ही नियंत्रित  रखना होगा। Water एवं water soluble molecules के free movement को रोकने के लिये cell membrane एक barrier के रुप में विकसित हुई।</p>
									
									<p class="mt-3 mb-2"><b> Structure of the cell membrane</b></p>
									<p class="mb-2"><b> 1. The Lipid bilayer</b></p>
									<p class="mb-2">तुम जानते हो कि lipids एवं water एक दूसरे में नहीं घुलते, इसीलिये प्रकृति ने cell membrane बनाने के लिये lipids को चुना जिससे इस lipid layer से होकर water movement न हो सके। अब lipids क्योंकि hydrophobic होते हैं, जो water के contact में भी नहीं रह सकते एवं इसे repel करते रहते हैं, इसीलिये membrane बनाने के लिये phospholipids भी चुने गये, जिनका एक छोर hydrophilic (किंतु lipophobic) phosphate group का हो, जो ECF के water के संपर्क में रह सके तथा दूसरा छोर hydrophobic (किंतु lipophilic) fatty acids का जो lipids के contact में भी रह सके। परंतु अब यही दिक्कत cell के भीतर होने लगी अर्थात hydrophobic fatty acids ICF के water के साथ किस प्रकार निभायें? इसके लिये cell membrane को double layered बनाना पड़ा होगा, जिसमें एक बाहरी layer हो जिसकी hydrophilic phosphate moiety ECF के संपर्क में तथा hydrophobic fatty acid moiety इसके भीतर हो तथा दूसरी भीतरी layer जो बाहरी layer से ठीक उल्टी हो, अर्थात दूसरी layer में hydrophilic phosphate moiety भीतर हो जो ICF के संपर्क में रह सके तथा hydrophobic fatty acid moiety बाहर। इस प्रकार जो double layered संरचना विकसित हुई उसकी बाहर से भीतर वास्तव में 4 layers हुई – <b>phosphate – fatty acid – fatty acid – phosphate</b>। इस प्रकार से phosphate group क्रमशः ECF एवं ICF के साथ रह सकेंगे एवं fatty acid groups परस्पर एक दूसरे के साथ।</p>

									<p class="mb-2">यह दो fatty acid layers वास्तव में एक दूसरे को attract भी करती हैं, जिससे दो phospholipid layers परस्पर जुड़ी रहती हैं। दोनों layers के मध्य में एक तीसरा component, cholesterol इनके मध्य cement का भी कार्य करता है तथा cell membrane को और अधिक water impermeable बनाता है। Cell membrane में एक अन्य प्रकार का lipid भी मिलता है, जो sphingosine नामक aminoalcohol से बना होने के कारण spingolipid कहलाता है। यह मुख्यतः nerve cells में मिलता है तथा signal transmission में भी सहायक होता 
									है।</p>									

									<p class="mb-2">इस प्रकार प्रत्येक cell membrane वास्तव में एक <b>lipid bilayer</b> है। इसकी संरचना को <b>fluid mosaic model</b> कहा जाता है, जो वास्तव में एक viscus fluid की तरह की आचरण करती है एवं अपने दोनों ओर विभिन्न substances को सहजता से आने जाने देती है। इस आवागमन की शर्त बस यही है कि वह substance lipid soluble होना चाहिये। कल्पना करो, एक बीकर में जल के ऊपर viscid oil की एक मोटी layer है। यह layer एक barrier का कार्य तो करेगी, परन्तु जिस प्रकार कोई वजन इस layer के पार नीचे जल में जा सकता है एवं दोबारा ऊपर खींचा भी जा सकता है, उसी प्रकार का आवागमन cell membrane की lipid bilayer से होकर भी हो सकेगा। जिस प्रकार oil की layer वजन के निकल जाने के उपरांत पुनः पहले की ही भांति हो जाती है, उसी प्रकार cell membrane भी lipid soluble substance के निकल जाने के बाद दोबारा पूर्ववत् barrier बना लेती है।</p>	

									<p class="mt-3 mb-2"><b>2. Integral and peripheral cell membrane proteins</b></p>				
									<p class="mb-2">परन्तु यह तो संभव नहीं कि किसी cell में केवल lipid soluble substances ही प्रवेश करें। आखिर water, ions एवं अन्य water soluble substances, cell के भीतर कैसे प्रवेश करेंगें? वास्तव में lipid soluble substances के cell membrane से होकर मुक्त आवागमन के विपरीत water soluble substances के लिए cell membrane में <b>channel proteins</b> के रूप में अलग, एक विशिष्ट व्यवस्था होती है। यह channels किसी द्वार (gate) के समान होते हैं जो सामान्यतः बंद रहते हैं। Cell की अपनी आवश्यकतानुसार उसे जिन-जिन substances को भीतर लेने अथवा बाहर निकालने की आवश्यकता होती है केवल उनके किये यह सीमित रूप से खुलते एवं बंद होते रहते हैं। विभिन्न पदार्थों के लिए यह channels भी भिन्न-भिन्न प्रकार के होते हैं अर्थात किसी एक पदार्थ के लिए specific होते हैं जैसे water channel अथवा sodium channel इत्यादि। इस प्रकार cell, water soluble substances के आवागमन को भी नियंत्रित कर सकती है। </p>

									<p class="mb-2">अनेक water soluble substances तो इन channel proteins के माध्यम से उनके concentration gradient के अनुसार diffusion के माध्यम से स्वतः ही निकल जाते हैं परन्तु कुछ पदार्थों को उनके concentration gradient के विरुद्ध लाना-ले जाना पड़ता है। इन पदार्थों को किसी अतिरिक्त सहायता की आवश्यकता पड़ती है। ऐसे पदार्थों को लाने-ले जाने के लिए कुछ channels, <b>carrier proteins</b> की भांति भी कार्य करती हैं। उपरोक्त channel एवं carrier proteins, cell membrane की बाहरी सतह से आरम्भ होकर, इससे गुजरती हुई भीतरी सतह तक पहुंचकर समाप्त होती हैं। अतः इन्हें <b>transmural proteins</b> कहते हैं (trans = एक ओर से दूसरी ओर; mural = दीवार) । इस प्रकार एक cell किसी घर के समान हुई जिसमें घर के व्यक्तियों के लिए कहीं से भी आने-जाने की छूट है जबकि बाहरी व्यक्तियों के लिए विशिष्ट द्वार हैं जो अनुमति मिलने पर ही खोले जाते हैं। कुछ बाहरी व्यक्ति इस द्वार से स्वतः ही प्रवेश कर लेते हैं जबकि कुछ अन्य को सहारा देकर लाना-ले जाना पड़ता है। </p>							
									<p class="mb-2">उपरोक्त व्यक्तियों के अतिरिक्त, कुछ व्यक्ति ऐसे भी होते हैं जिन्हें घर के अंदर नहीं बुलाया जाता। उनसे सन्देश लेकर उन्हें घर के बाहर से ही विदा कर दिया जाता है। इसी प्रकार, किसी cell पर भी शरीर के अन्य भागों से मिलने वाली विभिन्न सूचनाओं को ग्रहण करने के लिए <b>receptor proteins</b>, cell membrane पर ही होते हैं। इनमें कुछ receptor proteins transmural होते हैं जबकि कुछ अन्य cell membrane की बाहरी सतह पर ही लगे होते हैं। प्रत्येक receptor किसी विशिष्ट (specific) messenger molecules से ही जुड़ता है जिसे उस receptor का <b>ligand</b> कहते हैं। Transmural receptor proteins में इनको उत्तेजित करने वाला ligand, receptor के extracellular domain (भाग) से जुड़कर इसके intracellular domain को उत्तेजित करा देता है जो cell के भीतर ligand के कार्यों को संपन्न करता है। इनके विपरीत जो receptors transmural न होकर केवल cell membrane की बाहरी सतह पर ही लगे होते हैं, ligand से जुड़ने के बाद उन्हें ligand द्वारा लाये गए सन्देश को cell के भीतर पहुँचाने के लिए यह membrane की भीतरी सतह पर किसी अन्य messenger का उपयोग करना पड़ता है। ऐसी स्थिति में इस दूसरे messenger को <b>second messenger</b> कहते हैं जबकि <b>first messenger</b> वह ligand (या hormone) स्वयं कहलाता है।  </p>		

									<p class="mb-2">इनके अतिरिक्त, कुछ peripheral protein molecules किसी channel के ही extracellular domain से जुड़े रहते हैं तथा channel द्वारा हो रहे आवागमन को प्रभावित करते हैं। </p>		

									<p class="mb-2">उपरोक्त channel proteins, carrier proteins, transmural receptors एवं extracellular receptors, सभी, cell membrane की ही संरचना का भाग होने के कारण <b>integral protein molecules</b> कहलाते हैं। </p>

