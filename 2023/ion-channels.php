									<h5>Ion Channels</h5>					
																		
									<p class="mb-3">क्योंकि cations (मुख्यतः Na+, K+ एवं Ca++) एवं anions (मुख्यतः Cl-) किसी cell की bilayered lipid membrane से होकर नही गुजर सकते, अतः इनके आवागमन के लिये ions channels होती हैं। सामान्यतः cell को polarized अवस्था में ही बनाये रखने के लिये यह channels बन्द रहती हैं, परन्तु किसी activity के समय यह channels खुल जाती हैं, जिससे ions का passive movement उस cell में वह activity सम्पन्न करा सके।</p>
									
									<p class="mb-3">यह ion channels अपने-अपने ion (anion अथवा cation) के लिये specific होती हैं। यदि channel की भीतरी सतह पर positive charge होगा तब वह channel, positively charged cations को दूर ढकेलेगा एवं अपने से होकर नहीं गुजरने देगा। इन channel से होकर केवल anions ही गुजर सकेंगे, अर्थात् वह channel, anion specific हो जायेगा। इसके विपरीत यदि channel की भीतरी सतह पर negative charge होगा, तब वह channel, cation specific हो जायेगा।</p>

									<p class="mb-3">अब प्रश्न यह उठता है कि क्या किसी cation channel से सभी cations (Na, K+, Ca++ आदि) गुजर सकेंगे? वास्तव में नहीं। यह cation channels भी अपने-अपने cation के लिये specific होते हैं। ऐसा मुख्यतः इन channels के pore के diameter के कारण होता है। तुम जानते हो कि जैसे-जैसे किसी atom (अथवा उसके ion) का atomic number अथवा atomic weight बढ़ता जाता है, उसका आकार भी बढ़ता जायेगा। इसीलिये यदि किसी channel का diameter कम है तब वह केवल छोटे ions को ही गुजरने देगा, अपने diameter से बड़े diameter वाले ions को नहीं। इस प्रकार छोटे cations (जैसे Na+) केवल अपने ही Na+ channels द्वारा गुजर सकेंगे। अर्थात् Na+ channels केवल और केवल Na+ ions के लिये ही specific होगा।</p>

									<p class="mb-3">जरा सोचो, छोटे diameter वाले channels बड़े ions को निकलने न दें, यह बात तो समझ में आती है, परन्तु Na+ जैसे छोटे ions, K+ channels जैसे बड़े diameter के channels से क्यों नही निकल सकते? ऐसा इसलिए क्योंकि K+ channels के pore में कुछ <b>selectivity filters</b> लगे होते हैं जो उससे होकर केवल K+ को ही निकलने देते हैं। Bacteria की K+ channels वास्तव में 4 subunits की बनी tetrameric channel होती हैं जिनके मध्य में एक central pore होता है। इसी pore के बाहरी छोर पर एक pore loop में यह selectivity filter लगे होते हैं जो K+ channels के case में <b>carbonyl oxygen</b> के बने होते हैं। अब जैसे ही K+ इन pore loops के संपर्क में आता है, यह carbonyl oxygen उसके <b>hydrated ion</b> का water खींच लेते हैं। इस प्रकार size में छोटा हुआ K+, आसानी से K+ channel से निकल जाता है। अब प्रश्न यह उठता है कि यही carbonyl oxygen, hydrated Na+ का water क्यों नहीं खींच पाते? वास्तव में, hydrated Na+, hydrated K+ से छोटा होता है, जिससे वह selectivity filter के चारों carbonyl oxygens के संपर्क में नहीं आ पाता, जो उसे dehydrate कर पाते। Na+ channel में Na+ के dehydration के लिए दूसरी विधि का प्रयोग होता है। Na+ channels का strong internal negative charge ही Na+ को उससे जुड़े water molecule से अलग कर देता है।</p>
