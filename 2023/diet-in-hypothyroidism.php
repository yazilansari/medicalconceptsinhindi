									<p class="mb-2"><b>Hypothyroid</b> का आहार द्वारा <b>Management</b></p>

									<p>मानव शरीर एक अत्यधिक जटिल automatic मशीन है जो दो परस्पर सहयोगी systems: Nervous System और Endocrine System से उत्पन्न होने वाले अत्यंत small electrochemical signals  के माध्यम से संचालित, समन्वित और नियंत्रित होती है। Thyroid gland, endocrine system का एक हिस्सा है और इस gland  से निकालने वाले hormones का सामान्य से कम secretion होना hypothyroidism कहा जाता है। Hypothyroidism एक बहुत ही सामान्य शरीर विकार है और निम्नलिखित लक्षणों का कारण बनता है :-</p>

									<p class="mb-2"><b>Hypothyroidism</b> के लक्षण:</p>
									<p>Hypothyroidism के प्रमुख संकेत और लक्षण एक व्यक्ति से दूसरे व्यक्ति में भिन्न हो सकते हैं क्योंकि यह इस बात पर निर्भर करता है कि hormone के स्राव में कमी कितनी है। प्रारंभ में किसी को थकान और वजन बढ़ने जैसे लक्षण दिखाई दे सकते हैं और जैसे-जैसे metabolism धीमा होता जाता है, व्यक्ति को और अधिक स्पष्ट समस्याएं विकसित होती हुई मालूम पड़ सकती है, जैसे: weight बढ़ना, सुस्ती, depression और constipation |</p>


									<p class="mb-2">Hypothyroidism के कुछ बहुत ही प्रमुख चिह्न  और लक्षण हैं:</p>
									<p class="mb-1">- थकान</p>
									<p class="mb-1">- Weight बढ़ना</p>
									<p class="pl-4">- Increased sensitivity to cold</p>
