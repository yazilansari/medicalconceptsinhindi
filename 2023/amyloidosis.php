								<h5>Amyloidosis</h5>	
								<p>By Dr Divyansh Sharma And Dr Pankaj Kumar Agarwal</p>
								
								<p class="mb-2"><b>Amyloidosis</b></p>
								<p class="mb-3">Amyloidosis एक ऐसे disorders का समूह है जिसमे misfolded proteins का extracellular deposition हो जाता है। क्या आप बता सकते हैऐसा क्यों होता है? वास्तव में proteins का structure चार रूपों में पाया जाता है। Amino acids के chain या sequence में व्यवस्थित होने सेprimary structure बनता है जबकि इसी primary structure की बारम्बार folding से क्रमशः secondary, tertiary एवं quaternary structures बनते हैं। इसी protein folding के mechanism के गड़बड़ा जाने से ही misfolded proteins बनते हैं। सामान्य तौर पर cells इस misfolded protein को reprocess कर लेता है जिससे यह misfolded proteins, accumulate नहीं होते। किन्तु यदि किसी genetic mutation अथवाdisease के कारण से यह mechanism fail हो जाये, तो इन misfolded proteins, का अत्यधिक मात्रा में निर्माण होने लगता है। यह <b>misfolded proteins</b> cells के बाहर extracellular space में deposit होने लगती हैं। इसी को हम amyloidosis कहते हैं। वास्तव में amyloid शब्द amylum = starch एवं oid = like से मिल कर बना है जिसका अर्थ हुआ starch की तरह। ऐसा इसलिए क्योंकि सर्वप्रथम ये amyloid deposits देखने मेंstarch के deposits जैसे लगे थे जिनको बाद में proteins के रूप में पहचाना गया था। इस प्रकार amyloidosis का अर्थ हुआ, एक pathological process (suffix -osis = pathological process) जिसमें starch की तरह दिखने वाला पदार्थ जमा हो रहा हो। </p>

								<p class="mb-3">Electron microscopy से amyloid, लगभग 10 nm diameter के continuous, non-branching fibrils के रूप में दिखता है। Xray crystallography एवं infrared spectroscopy में इसको एक विशेष प्रकार के <b>'antiparallel'</b> arrangement में, Beta <b>pleated sheets</b> के रूप मेंव्यवस्थित देखा जाता है।</p>			

								<p class="mb-2"><b>Pathogenesis</b></p>

								<p class="mb-2">अब प्रश्न यह उठता है कि यह misfolded proteins, किसी disorder को उत्पन्न किस प्रकार करती हैं? वास्तव में, misfolded proteins के कुछmolecules परस्पर जुड़ कर oligomers बना लेते हैं जो बाद में polymers एवं फिर fibrils बनाते जाते हैं। जहाँ normally folded proteins water soluble होते हैं वहीँ यह misfolded proteins, extracellular fluid में insoluble होते हैं। यह extracellular matrix के proteoglycans एवंglycosaminoglycans (heparan sulfate एवं dermatan sulfate) से जुड़कर extracellular tissue में ही deposit हो जाते हैं।  इनमें oligomers सर्वाधिक हानिकारक होते हैं। यह reactive oxygen species का निर्माण करके cellular stress को बढ़ाते हैं। Fibrils तो एक बार deposit होने केबाद किसी tissue में हमेशा के लिए बने रहते हैं एवं उस organ की functioning में बाधा डालते रहते हैं। </p>
								
																
								<p class="mb-3">जैसा कि तुम पढ़ चुके हो, misfolded proteins ही insoluble होने के कारण amyloid के रूप में extracellular space में deposit हो जाती हैं।यहाँ प्रश्न यह उठता है कि जब यह misfolded proteins, cells के अंदर बनती हैं तब वह misfolded होने पर intracellularily ही deposit क्यों नहींहो जाती? वास्तव में cells के proteasomes इनको degrade करके cells से remove करा देते हैं। यूं तो extracellular space में भी इनकेdeposits, macrophages द्वारा remove किये जाते हैं परन्तु यदि इनका निर्माण अत्यधिक मात्रा में होने लगता है तब यह अतिरिक्त amyloid, extracellular space में deposit होने लग जाता है। AL एवं AA amyloid के deposits इसी प्रकार बनते हैं। इनके अतिरिक्त कुछ mutant proteins सामान्य मात्रा में बनने पर भी abnormal होने के कारण deposit होने लगते हैं। ऐसा transthyretin, ATTR amyloid के साथ होता है। </p>					
								
