									<h5>Erythropoiesis</h5>					
																		
									<p class="mb-3"><b>Colony forming unit–erythrocytes (CFU-E)</b> ही RBC series की committed stem cell (CSC) है जिसके division एवं differentiation से RBCs का निर्माण होता है। यह एक समूह के रूप में बड़ी तीव्रता से multiply करती हैं। यह समूह वास्तव में एक organ की भांति कार्य करता है जिसे erythron कहते हैं। सामान्यतः एक cell के 4-5 successive divisions से 16-32 RBCs उत्पन्न होती हैं।</p>
									
									<p class="mb-3">RBC series की पहली cell, proerythroblast है। यहीं से hemoglobin निर्माण की प्रक्रिया आरम्भ होती है एवं साथ ही nucleus, mitochondria एवं endoplasmic reticulum के absorption की। इस प्रकार, जैसे-जैसे proerythroblasts के divisions से RBC series की अगली stages (early normoblasts → intermediate normoblasts → late normoblasts) का निर्माण होता जाता है, उनमें hemoglobin की मात्रा बढ़ती जाती है तथा cell organelles की संख्या एवं आकार छोटा होता जाता है।</p>
									
									<p class="mb-3">ध्यान रहे, nucleus acidic होने के कारण basic एवं positively charged methylene blue dye से react कर blue stain लेता है (basophillic)। इसके विपरीत, hemoglobin, basic होने के कारण acidic एवं negatively charged eosin dye से react कर pink stain लेता है (eosinophillic)। इस प्रकार, जैसे-जैसे nucleus का size छोटा होता जायेगा, cell का blue stain कम होता जायेगा। साथ ही, जैसे-जैसे hemoglobin की मात्रा बढ़ती जाएगी, cell का pink stain बढ़ता जायेगा। इस प्रकार, RBC series की अगली cells, basophillic से polychromatic होते हुए orthochromatic erythroblasts बनाती जायेंगीं। इस प्रकार, early, intermediate एवं late normoblasts को हम उनके color के अनुसार दूसरे नामों से भी पहचान सकते हैं, <b>basophil erythroblasts → polychromatophil erythroblasts → orthochromatic erythroblasts ।</b> </p>									
									<p class="mb-3">Nucleus के छोटे होते जाने के साथ-साथ cell का size भी कम होता जाता है। Nucleus के लुप्त हो जाने के बाद भी कुछ cell orgenelles, cell में बचे रहते हैं जिससे stained cell में कुछ reticular appearance (जाल के समान) दिखलाई पड़ने लगती है। इस कारण इस stage को <b>reticulocyte</b> कहते हैं। यह reticulocyte ही bone marrow से निकलकर blood में प्रवेश करती है। Blood में आने के बाद, एक-दो दिनों में ही इसका reticular material भी लुप्त हो जाता है, जिससे reticulocyte, <b>mature RBC</b> में बदल जाती है।सामान्यतः 0.8-1% RBCs प्रतिदिन टूटती रहती हैं जो नयी RBCs से बदलती रहती हैं।</p>									
									<p class="mb-3"><b>Regulation of erythropoiesis</b> </p>									
									<p class="mb-3">Committed stem cell बनने के बाद की पूरी प्रक्रिया erythropoietin (EPO) द्वारा नियंत्रित होती है जो kidneys की peritubular capillary lining से secrete होता है। Hb conentration 10 gm/dl से कम होने पर जब kidneys (एवं अन्य tissues) की oxygen delivery घटने लगती है तब hypoxia inducible factor (HIF) के प्रभाव में EPO का secretion बढ़ने लगता है जिससे erythropoiesis को बढ़ा कर oxygen delivery को पुनः सामान्य अवस्था में लाया जा सके। EPO के प्रभाव में RBC production, 1-2 weeks में 4-5 गुना तक बढ़ाया जा सकता है।</p>									
									
