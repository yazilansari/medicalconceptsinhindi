									<h5>थायराइड ओप्थाल्मोपैथी के Eyesigns</h5>
									<p class="mb-4"><u>थायराइड ओप्थाल्मोपैथी के eyesigns</u></p>								
									
									
									<p class="mb-3">ग्रेव्स डिजीज में आंखों में उत्पन्न होने वाले परिवर्तनों को graves ophthalmopathy  कहा जाता है जिसका अर्थ है ग्रेव्स डिजीज में आंखों में होने वाला रोग। ग्रेव्स डिजीज में TSH रिसेप्टर के विरुद्ध बनने वाली ऑटो एंटीबॉडीज  ऑर्बिट के फैट एवं मांसपेशियों से भी cross react  करके उन में सूजन उत्पन्न करती हैं जिससे उनका आयतन बढ़ता है एवं आंखें बाहर निकल आती है। इसे exophthalmos कहते हैं। थायराइड ओप्थाल्मोपैथी के विभिन्न eyesigns निम्नलिखित हैं-</p>

									<p class="mb-2 dotedt2"><span>1.</span> पलक के लक्षण ( Lid signs)-</p>
									<p class="mb-3">Dalrymple’s Sign: यह चिन्ह आँखों की एक घूरने वाली उपस्थिति ( staring look) का वर्णन करता है, जोकि  palpebral fissure के चौड़ा होने कारण होता है। यह चिन्ह muller muscle की अति सक्रियता के कारण होता है| sympathetic stimulation के कारण ऊपरी पलक की Mullers muscle  के संकुचन से ऊपरी पलक और ऊपर  खींच जाती है , तो इस स्थिति को lid retraction कहा जाता है।</p>
									
									<p class="mb-3"><b>Von Graefe’s Sign:</b> जब मरीज नीचे की तरफ देखता है , तो ऊपरी पलक और कॉर्नियल लिम्बस के मार्जिन के बीच सफेद sclera  दिखाई देती  है। इसे lid lag sign बोलते है | यह  चिन्ह sympathetic 
									<p class="mb-3"><b>Vigoroux Sign:</b> ऊपरी पलकों का भरा  हुआ होना /  ऊपरी पलको के ऊपर सूजन आना । (Figure 1)</p>
									<p class="mb-3"><b>Enroth Sign:</b> निचली  पलकों के ऊपर सूजन आना (Figure 1)</p>


									<p class="mb-3">Grove Sign-  ऊपरी पलकों को नीचे लाने में दिक्कत महसूस होना। </p>

									<p class="mb-2">Rosenbach's Sign: आंखों को बंद करने के पश्चात ऊपरी पलकों के ऊपर महीन झटके महसूस होना । </p>

									<p class="mb-2">Gifford’s Sign: आँख की ऊपरी पलक को मोड़ने में कठिनाई  महसूस होना ।</p>

									<p class="mb-2">Boston’s Sign: नीचे की ओर देखने पर ऊपरी पलक की झटकेदार अनियमित गतिविधि होना।</p>

									<p class="mb-2">Kocher’s Sign:  विजुअल फिक्सेशन के साथ  पलक के  रिट्रेक्शन का बढ़ना(Figure 2)</p>									
									<p class="mb-2">Abadie Sign: लेवेटर पैल्पेब्रे सुपीरियरिस पेशी की ऐंठन  होने के साथ ऊपरी पलक  का पीछे 
									हटना।</p>
									<p class="mb-4">Riesman's sign: स्टेथोस्कोप के साथ बंद आंख पर ब्रूट की आवाज सुनाई देती है।</p>
									

									<p class="mb-3"><b><u>चेहरे के निशान(Facial Signs)-</u></b></p>
									<p class="mb-3">Stellwag Sign: अधूरा और  पलकों का कम झपकना ( घूरने जैसी स्थिति लगना)।</p>
									<p class="mb-4">Joffroy Sign:  ऊपर देखने पर माथे पर   त्वचा की रेखाओं का अनुपस्थित होना। (Figure3)</p>
									
									
									<p class="mb-2">Jellinek's Sign:  ऊपर  के  eyefolds का हाइपरपिगमेंटेशन होना।</p>

									<p class="mb-2">Hertoge’s Sign: भौहों के lateral तीसरे भाग का का गायब होना।</p>

									<p class="mb-5">Sainton’s sign:  ऊपर देखने पर  माथे पर  देर से झुर्रियाँ  आना।</p>

									

									<p class="mb-3"><b>एक्स्ट्रा-ओकुलर मसल साइन्स (Extra-Ocular Muscle Signs)-</b></p>
									<p class="mb-4">Möbius Sign: अभिसरण ( converge) करने में असमर्थता। (figure 4)</p>
									
									<p class="mb-2">Ballet Sign -एक या एक से अधिक आंख की  बाहरी मांसपेशियों ( extraocular muscles) का प्रतिबंध।</p>

									<p class="mb-2">Jendrassik's Sign: नेत्रगोलक ( eyeball) के abduction  और rotation  मैं दिक्कत आना।</p>

									<p class="mb-4">Suker’s Sign: आंखों का abduction  करने पर fixation   सही से न होना।</p>
									<p class="mb-2"><u><b>पुतली के लक्षण (Pupillary Signs)-</b></u></p>
									
									<p class="mb-2">Cowen’s Sign: प्रकाश की ओर देखने पर पुतली का झटकेदार संकुचन होना।</p>

									<p class="mb-2">Lowy’s Sign: आंखों में थोड़ी मात्रा में एड्रीनलिन डालने पर आंखों की पुतलियों का  चौड़ा हो जाना जाना। </p>
									<p class="mb-4">Knies’s sign:  मंद प्रकाश में  दोनों  पुतलियों  का  असमान  फैलाव होना। </p>


									<p class="mb-2"><b>अन्य लक्षण -</b></p>
									<p class="mb-2">Goldzeiher’s Sign: bulbar conjuctiva की blood vessles  का prominent होना। (Figure 5)</p>
									<p class="mb-3">Payne Trouseau Sign: orbit से नेत्र गोलक का बाहर आना।</p>
									<p class="mb-3">Sattler Sign:  ऊपर की ओर देखने पर आंखों में के इंट्राऑकुलर प्रेशर का बढ़ना।</p>
									


