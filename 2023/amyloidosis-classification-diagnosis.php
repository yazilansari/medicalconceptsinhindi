									<h5>Amyloidosis - Classification And Diagnosis</h5>	
									<p>By Dr Divyanshu Sharma And Dr Pankaj Kumar Agarwal</p>
									
									<p class="mb-2"><b>Types of amyloidosis</b></p>
									<p class="mb-3">यह amyloidosis, genetic या acquired, कई कारणों से हो सकता है। यह प्रमुखतः दो प्रकार का हो सकता है- (1) Localised (यानी किसी एकtissue/organ में) और (2) Systemic (यानी शरीर में कई जगह)। यहां पर हमें amyloidosis का nomenclature समझना जरूरी है। Amyloidosis को हम 'AX' से denote करते हैं जिसमे A=Amyloid औऱ X=type of protein है।इसके कुछ उदाहरण निम्नांकित हैं -</p>

									<p class="mb-2">Localised amyloidosis</p>									
									<p class="mb-2">Alzheimer's disease : ABeta; beta type amyloid के deposition से </p>
									<p class="mb-2">Diabetes mellitus : AIAPP; islet amyloid polypeptide (amylin) के deposition 
									से </p>
									<p class="mb-2">Medullary carcinoma thyroid : ACal; calcitonin के deposition से </p>
									<p class="mb-3">Atrial fibrillation : AANF; atrial natriuretic polypeptide के deposition से </p>


									<p class="mb-2"> <b>Systemic amyloidosis</b> </p>

									<p class="mb-2">Primary amyloidosis : Type AL - L=light chain of immunoglobulin; यह myeloma एवं lymphoma में मिलता है </p>

									<p class="mb-2">Secondary या reactive amyloidosis : Type AA - A = acute phase reactant; यह chronic inflammatory एवं infective disorders मेंमिलता है </p>

									<p class="mb-2">Familial amyloidosis : Type AF - F = familial; यह transthyretin (TTR) या retinol binding protein के mutation में मिलता है  </p>

									<p class="mb-2">Hemodialysis associated : Type ABeta2M - Beta2M = beta2 microglobulin; यह लम्बे समय तक hemodialysis कराते रहने के बाद मिलता है </p>
									
									<p class="mb-3">आओ कुछ मुख्य प्रकार की amyloid proteins के विषय में विस्तार से समझते हैं। </p>

								
									<p class="mb-2"><b>AL amyloidosis</b></p>
									<p>यह amyloidosis की commonest form  है जिसमें immunoglobulins की light chain के deposits बनते हैं। यह misfolded light chains शरीर के अनेकों भागों में deposit हो सकती हैं जिसके कारण इसे <b>primary systemic amyloidosis</b> भी कहते हैं। यह light chains, bone marrow में clonal B cells अथवा plasma cells के द्वारा बनते हैं। इन्हीं cells के monoclonal expansion से ही multiple myeloma एवंlymphoma उत्पन्न होते हैं। वास्तव में इनमें अंतर मात्र इतना ही है कि multiple myeloma में यह M proteins अथवा kappa या lambda light chains, properly folded होते हैं जबकि AL amyloidosis में light chains, misfolded । यह AL amyloid मुख्यतः heart, lungs, GIT, skin एवंtongue में deposit होते हैं। </p>

									<p class="mb-2"><b>AA amyloidosis</b></p>
									<p class="mb-3">इसके विपरीत, जब किसी chronic inflammatory अथवा infective state में बनने वाली acute phase reactant proteins के रूप में liver के द्वाराserum amyloid A (SAA) का निर्माण होता है तब इससे उत्पन्न होने वाली condition को <b>AA amyloidosis</b> कहते हैं। क्योंकि यह किन्हीं दूसरेdisorders के द्वारा उत्पन्न होती है इसलिए इसे <b>secondary amyloidosis</b> भी कहते हैं। यह AA amyloid मुख्यतः kidneys, liver, spleen, lymph nodes, adrenals एवं thyroid में deposit होते हैं। Amyloid के deposits के कारण spleen दानेदार दिखती है जिसे <b>sago</b> (साबूदाने कीभांति) <b>spleen</b> कहते हैं। Cut section में भी यह deposits किसी नक्शेनुमा, चर्बी के ढेर जैसे दीखते हैं जिसके कारण इस appearance को <b>lardaceous</b> (चर्बी की भांति) <b>spleen</b> कहते हैं। </p>


									<p class="mb-2"><b>ABeta amyloidosis</b></p>
									<p class="mb-3">Cerebrum में amyloid precursor protein के द्वारा Beta amyloid protein का निर्माण होता है जो अधिक होने पर cerebral tissue एवं इसकीblood vessels में deposit होने लगता है। इसी से Alzheimer disease उत्पन्न होती है। </p>

									<p class="mb-2"><b>Symptoms and signs</b></p>
									<p class="mb-2">क्योंकि amyloid protein शरीर में कहीं भी deposit हो सकता है इसलिए इससे प्रभावित रोगी विभिन्न प्रकार के symptoms के साथ आ सकते हैं।यह इस पर निर्भर करेगा कि amyloid कौन से organ/tissue में deposit हो रहा है। </p>

									<p class="mb-2">Kidney - (सर्वाधिक प्रभावित होने वाला organ) - glomerular involvement से proteinuria, hypoalbuminemia, edema इत्यादि। </p>
									<p class="mb-2">Heart में cardiomyopathy से concentric hypertrophy, diastolic dysfunction एवं congestive heart failure</p>
									<p class="mb-2">Liver में hepatomegaly एवं cholestasis</p>
									<p class="mb-2">Spleen में splenomegaly </p>
									<p class="mb-3">GIT के deposits से diarrheoa एवं malabsorption</p>

									<p class="mb-2"><b>Diagnosis </b></p>
									<p class="mb-3">Amyloidosis की diagnosis सामान्य तौर पर clinical suspicion से शुरू होती है एवं इसका confirmation, प्रभावित tissue केhistopathological तथा immunohistochemical examination के द्वारा किया जाता है। इस प्रभावित tissue के लिए यदि रोगी के symptoms केअनुसार amyloidosis का localisation संभव हो तब उस संबंधित organ की biopsy की जाती है जबकि systemic amyloidosis में इस प्रभावितtissue के मिलने की सर्वाधिक सम्भावना abdominal fat में होती है। </p>

									<p class="mb-2"><b>Histopathology </b></p>
									<p class="mb-3">किसी histopathological tissue को सबसे पहले तो <b>hematoxylin</b> एवं <b>eosin staining</b> के बाद light microscope से देखा जाता है। इसमेंamyloid एक <b>amorphous, eosinophilic, hyaline substance</b> के रूप में दिखता है। क्योंकि extracellular collagen एवं fibrin भी इसीप्रकार के hyaline substance के रूप में दिखते हैं इसलिए amyloid को इन normal constituents से differentiate करना बहुत आवश्यक है।इसके लिए tissue को amyloid specific <b>Congo red dye</b> से stain करके पुनः देखा जाता है। Ordinary light में तो यह pink या red color कादिखता है परन्तु <b>polarised light</b> में देखने पर इससे निकलने वाली <b>apple green birefringence,</b> amyloid substance के लिए specific होतीहै जो कि इस protein के विशेष beta-pleated sheet structure के कारण से मिलती है। इसके विपरीत सामान्य प्रकार से folded protein, white ही दिखता है।  </p>
									
									<p class="mb-2"><b>Immunohistochemistry</b></p>
									<p class="mb-2">Amyloid protein के detection के पश्चात, diagnosis का अगला चरण है यह ज्ञात करना कि यह amyloid protein किस प्रकार का है। इसके लिएविभिन्न प्रकार की amyloid proteins के विरुद्ध बनी specific antibodies को किसी enzyme अथवा fluorescent material से tag करके प्रभावितtissue पर apply करते हैं। अब उस tissue में जिस प्रकार की amyloid protein होगी, वह उसी प्रकार की antibody से react करेगी।  </p>
									<p class="mb-3">Abdominal fat एवं invasive tissue biopsy के negative होने पर amyloidosis की diagnosis को rule out किया जा सकता है।</p>
									

									
