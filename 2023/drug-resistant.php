									<p class="mb-2"><b>Approach to Diagnosis</b>-:</p>									
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> सारे TB के मरीजों सारे presumptive मरीजों और सारे non responders का सर्व प्रथम CBNAAT टेस्ट कराएं।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसमें sputum positivity के साथ Rifampicin resistance आसानी से चेक किया जा सकता है।</p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> अब यदि FL LPA की रिपोर्ट आई और उसमे Isoniazid resistance भी पाया गया तो ऐसेमे Bedaquiline वाले shorter oral MDR or RR regimen चलाए जाते हैं। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसके बाद यदि भेजी हुई जांचों में कोई नया resistance या फिर मरीज की drug tolerability ना हो तो modify किए जाते हैं ये regimen और जरूरत पड़ने पर Longer Oral MDR/XDR रेजिमेन चलाए जाते हैं। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसके विपरीत यदि FL-LPA की रिपोर्ट में Isoniazid और Fluoroquinolones दोनो resistance प्राप्त हुए तो बिना देरी के सीधा Longer Oral MDR / XDR रेजिमेन चलाने चाहिए।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसी प्रकार यदि Rifampin resistance ना हो तो FL LPA करवाकर Isoniazid resistance की जांच करें यदि यह पाया गया तो H-mono या Polydrug TB रेजिमेन शुरू कर SL LPA करवाएं और उसके हिसाब से कोई अन्य resistance को ढूंढने की कोशिश करें और वर्तमान रेजिमेन को उसके अनुसार modify करें और non responders पर नजर रखें।</p>																	
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इन मरीजों को जो भी H-mono/Poly/ Multiple/Xtreme drug resistance TB regimen पर हैं उन्हे हर तीन महीने पर अपनी बलगम की AFB और culture रिपोर्ट करवानी चाहिए और उसके हिसाब से drug रेजिमेन modify करने चाहिए।</p>																	

