									<p class="mb-2"><b>Spirometry  </b></p>
									<p class="mb-4">यह अत्यंत सरल प्रक्रिया है जिसके द्वारा हम lungs की expiratory power का आकलन कर सकते हैं। इसके द्वारा obstructive एवं restrictive lung disorders में विभेद किया जा सकता है। रोग के आरम्भ में ही यह उसकी दशा एवं तीव्रता की जानकारी दे सकता है एवं इसके द्वारा उपचार के परिणामों का आकलन भी किया जा सकता है। </p>
									<p class="mb-2"><b>Procedure </b></p>
									<p class="mb-2">Spirometry एक सरल जांच है जिसे किसी डॉक्टर के अतिरिक्त कोई अन्य प्रशिक्षित टेक्नीशियन भी कर सकता है। सबसे पहले रोगी को जांच की पूरी प्रक्रिया समझा दें क्योंकि यह एक effort dependent test है जिसके लिए यह आवश्यक है कि रोगी जांच की विधि को ठीक प्रकार से समझकर उसे भली-भांति करे। उसके कपड़े अधिक कैसे हुए न हों एवं वह मूत्र विसर्जित कर के आराम से बैठे। </p>
									<p class="mb-2">Spirometry की प्रक्रिया 4 चरणों में होती है। </p>
									<p class="mb-2">2. पूरे जोर से जितनी तीव्रता से फूंक सकते हैं उतनी तीव्रता से फूंकें। </p>
									<p class="mb-2">3. आरम्भ में तीव्रता से फूंकने के पश्चात् भी लगातार सांस छोड़ते रहें।  </p>
									<p class="mb-2">4. पूरी साँस छोड़ने के पश्चात अंत में लम्बी साँस अंदर लें। </p>
									<p class="mb-4">यह क्रिया लगभग 6 सेकंड में करनी होती है।</p>
									
									<p class="mb-2"><b>Precautions </b></p>
									<p class="mb-4">इस जांच से प्राप्त परिणामों का उचित रूप से आकलन करने के पूर्व यह अवश्य सुनिश्चित कर लेना चाहिए कि जांच उचित प्रकार से की गयी है अथवा नहीं। </p>
									
									<p class="mb-4"><b>Acceptability criteria</b> - अधिकतम गहरी सांस ली गयी हो; फूंकने के दौरान पूरा जोर लगाया गया हो; पहले सेकंड में कोई हिचकिचाहट या रुकावट न आयी हो; एवं expiration 6 सेकंड तक चलता रहा हो; इस दौरान खांसी न आयी हो; एवं फूंकने के दौरान air leak न हुई हो। </p> 
									
									<p class="mb-4"><b>Reproducibility criteria</b> - जांच को अनेक (अधिकतम 8) बार दोहरा कर किया जा सकता है। इनमें भली-भांति की गयीं 3 जांचों का चयन करते हैं जिनमें FVC और FEV1 सर्वाधिक आ रहा हो एवं इनकी अधिकतम मात्रा में 150ml से अधिक का अंतर न हो। </p>

									<p class="mb-2"><b>Interpretations of results</b></p>
									<p class="mb-4">Spirometry से प्राप्त graphs दो प्रकार के हो सकते है-  flow volume graph एवं volume time graph  </p>

									<p class="mb-2"><b>Lung parameters inferred from spirometry </b></p>
									<p class="mb-3">Spirometry के माध्यम से जिन lung parameters की गणना की जा सकती हैं, वे निम्नांकित हैं -</p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Forced vital capacity (FVC)</b> - इसके लिए रोगी को कहते हैं कि वह अपनी पूरी क्षमता से गहरी सांस लेकर फिर उसे पूरी क्षमता से, अधिकतम मात्रा में बाहर निकाले। इससे lungs की उस क्षमता को नापा जा सकता है कि किसी गहरी सांस में वह अधिकतम कितनी वायु को अंदर खींच सकता है अथवा बाहर निकाल सकता है। सामान्यतः पुरषों में यह 4.8L एवं महिलाओं में 3.1 L होती है।</p>
									
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Forced expiratory volume in 1 second (FEV1)</b> - इसके लिए भी रोगी को अपनी पूरी क्षमता से गहरी सांस लेकर फिर उसे पूरी क्षमता से, एवं पूरी तीव्रता से बाहर निकालने को कहते हैं। FVC के लिए जहाँ expiratory air के पूरे volume (FEV) को नापा जाता है वहीँ FEV1 के लिए expiratory air के केवल पहले सेकंड में निकले volume को ही नापा जाता है। सामान्यतः पहले सेकंड में ही कोई व्यक्ति, कुल expiratory volume का 83% (70-90%) भाग बाहर निकाल सकता है। </p>

									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Peak expiratory flow rate (PEFR)</b> - इस प्रकार हम समझ सकते हैं कि forceful expiration के पहले सेकंड में ही वायु की अधिकतम मात्रा बाहर निकल जाती है जिसके पश्चात् expiratory flow में बड़ी तीव्रता से कमी आती जाती है। इसका अर्थ यह हुआ कि यदि forceful expiration के दौरान यदि expiratory air के flow rate की गणना की जाए तब वह पहले सेकंड में ही सर्वाधिक होगा। इसे ही peak expiratory flow rate (PEFR) कहते हैं। वास्तव में इस दौरान लगभग 25% वायु (forced expiratory flow - FEF 25%) बाहर निकलती है जो major airways (pharynx, larynx, trachea एवं primary bronchi) में भरी रहती है एवं बिना किसी resistance के बड़ी सरलता से बाहर निकल आती है। इसके पश्चात् निकलने वाली लगभग 75% वायु (FEF 25-75%) minor airways (secondary एवं tertiary bronchi एवं bronchioles) के resistane से होते निकलने के कारण अपेक्षाकृत धीमे रूप से अगले 2-3 सेकंड में निकल पाती है। </p>

									<p class="mb-3"><b>Indications -</b> Spirometry की आवश्यकता निम्नांकित परिस्थितियों में पड़ती है - </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Chronic obstructive airway disorders (COAD)</b> - रोग की पहचान के लिए (detection), इसकी गंभीरता के आकलन के लिए (quantification) एवं उपचार से होने वाले परिणाम को जानने के लिए (monitoring) </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Restrictive lung disorders (RLD)</b> - detection, quantification एवं monitoring के लिए</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Occupational lung disorders (OLD)</b> - इनके विभिन्न प्रकारों को एक दूसरे से अलग-अलग पहचानने के लिए (differentiation)   </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Preoperative assessment</b> - किसी सर्जरी के पूर्व रोगी की ventilatory capacity के आकलन के लिए कि वह सर्जरी करा पाने के लिए योग्य है अथवा नहीं </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Smoking</b> - धूम्रपान से हुई हानि को जानने के लिए एवं उसको बंद करने के पश्चात् हुए लाभ के आकलन के लिए  </p>
									
									
									<p class="mb-3"><b>Absolute contraindications</b> - जिसमे यह प्रक्रिया बिलकुल भी नहीं करनी चाहिए जैसे </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Recent myocardial infarction - क्योंकि प्रक्रिया के दौरान होने वाले श्रम से angina उत्पन्न हो सकता है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Hemoptysis - क्योंकि जोर से फूंकने के दौरान bleeding पुनः आरम्भ हो सकती है।   </p>
									
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Pneumothorax - क्योंकि जोर से फूंकते समय pneumothorax बढ़ सकता है। </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Active pulmonary tuberculosis - क्योंकि रोगग्रस्त स्थान bleeding हो सकती है या वह फट कर pneumothorax या hemothorax उत्पन्न कर सकता है।</p>
									

									<p class="mb-3"><b>Relative contraindications</b> - जिनमें अत्यंत आवश्यक न होने पर यह जांच नहीं करनी चाहिए -</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Unstable cardiovascular or respiratory status - क्योंकि इनमें वृद्धि हो सकती है।  </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Thoracic, abdominal or cerebral aneurysm - क्योंकि जोर लगाने से aneurysm फट सकता है।  </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Recent eye surgery - क्योंकि जोर लगाने से आँखों में bleeding हो सकती है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Uncooperative patient -  क्योंकि अनुचित रूप से हुई जांच से सही परिणाम नहीं निकाला जा सकता है।</p>
									

									<p class="mb-2"><b>Side effects - </b>चेहरा लाल होना, सिर में भारीपन, चक्कर आना या मूत्र निकल जाना।</p>
									
