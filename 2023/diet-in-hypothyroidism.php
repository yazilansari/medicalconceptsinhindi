									<p class="mb-2"><b>Hypothyroidism</b> का पता कैसे लगाएं?</p>

									<p class="mb-3">Thyroid gland की कार्यप्रणाली एक रसायन द्वारा नियंत्रित होती है जिसे thyroid stimulating hormone (TSH) कहा जाता है और जो pituitary gland नामक एक अन्य gland द्वारा स्रावित किया जाता है। रक्त में TSH का स्तर हमें thyroid gland की सक्रियता के बारे में जानकारी देता है।</p>

									<p class="mb-3">TSH के स्तर के लिए सामान्य सीमा 0.4 और 4.5 milli-international units per liter के बीच है। thyroid विकार के लिए पहले से ही इलाज करा रहे लोगों के लिए, इन स्तरों को प्रति लीटर 0.5-3.0 milli-international units per liter की सीमा में होना चाहिए।</p>

									<p class="mb-3">यदि किसी व्यक्ति के रक्त में TSH का स्तर इसके सामान्य परिसर से कम पाया जाता है तो वह व्यक्ति hypothyroidism से पीड़ित है और deviation जितना अधिक 	होता है रोग की अवस्था भी उतनी ही अधिक गंभीर होती है।</p>

									<p ><b>Hypothyroidism</b> के संभावित कारण</p>
									<p ><b>Hypothyroidism</b> मूल रूप से जीवन शैली से संबंधित बीमारी है। <b>Hypothyroidism</b> के संभावित कारणों को नीचे सूचीबद्ध किया गया है:</p>

									<p class="mb-2"><b>1. Congenital hypothyroidism</b></p>
									<p>जब बच्चा hypothyroidism के साथ पैदा होता है, तो या तो उसमें अविकसित thyroid gland होती है या उसकी यह gland अपने optimal स्तर पर कार्य नहीं करती है, इस स्थिति को congenital hypothyroidism कहा जाता है। यह स्थिति बच्चे को उसके माता-पिता से विरासत में मिलती  है जो प्रायः autoimmune विकारों से पीड़ित हो सकते हैं। बाहरी रूप से बच्चा जन्म के बाद बिल्कुल सामान्य लग सकता है, लेकिन इसका gland दोषपूर्ण हो सकता है। इसलिए जन्म के समय thyroid की जांच अनिवार्य है।</p>

									<p class="mb-2"><b>2. अपर्याप्त शारीरिक गतिविधि</b></p>
									<p>जब आप शारीरिक गतिविधि कम करते हैं , व्यायाम नहीं करते और आलसी जीवन शैली का पालन करते है, तो यह hypothyroidism की जोखिम का कारण बन जाता है। अपनी पाचन क्रिया, hormones से संबंधित neurological functions तथा पुराने तनाव का प्रबंधन करने के लिए प्रत्येक व्यक्ति को नियमित रूप से व्यायाम करने की आवश्यकता होती है,  । व्यायाम बेहतर नींद में मदद करता है, स्वस्थ वजन बनाए रखता है और तनाव कम करता है।</p>

									<p class="mb-2"><b>3. पाचन तंत्र की सूजन</b></p>
									<p class="mb-2">जब पाचन तंत्र अस्वस्थ होता है या इसके किसी अंग में सूजन आ जाती है तो इससे शरीर में पोषण की कमी हो जाती है, autoimmune system कमजोर पड़ जाता है और बाहरी संक्रमणों का ख़तरा बढ़ जाता  है।</p>

									<p>आहार तंत्र को स्वस्थ्य बनाए रखना वास्तव में महत्वपूर्ण है। आपकी आंत के स्वास्थ्य को प्रभावित करने वाले कारक हैं: gut bacteria का संतुलन , आहार में toxin पदार्थों या chemicals की उपस्थिती  तथा तनाव का उच्च स्तर जिसके परिणामस्वरूप आपके आहार तंत्र  के स्वास्थ्य पर भारी नकारात्मक प्रभाव पड़ता है और पाचन तंत्र को नुकसान पहुंचता है जो आगे चलकर hypothyroidism जैसी स्थितियों की ओर ले जाता है। ।</p>


									<p class="mb-2"><b>4. आहार में महत्वपूर्ण पोषक तत्वों की कमी</b></p>
									<p class="mb-2">कम संभावना है फिर भी हो सकता है कि आपके आहार में selenium और iodine जैसे पोषक तत्वों की पर्याप्त मात्रा में कमी हो। चूँकि ये पोषक तत्व thyroid की कार्यक्षमता के लिए जिम्मेदार होते हैं, इनकी कमी thyroid विकारों को जन्म दे सकती है।</p>

									<p class="mb-3">इसी तरह, ये पोषक तत्व आपके शरीर को अन्य समस्याओं से बचाते हैं। उदाहरण के लिए, यदि आपके शरीर में selenium की भारी कमी हो जाती है, तो आपके thyroiditis से पीड़ित हो जाने की अधिक संभावना बढ़ जाती  है। यह एक प्रबल antioxidant  glutathione को active होने से रोकता है । Glutathione सूजन और oxidative stress से लड़ने के लिए जिम्मेदार है।</p>

									<p class="mb-2"><b>5. दवा</b></p>
									<p class="mb-3">कई दवाएं हैं जो hypothyroidism के लक्षणों को बढ़ाने में योगदान करती हैं । उदाहरण के लिए, lithium का उपयोग डॉक्टरों द्वारा विशिष्ट psychiatric विकारों के इलाज के लिए किया जाता है। साथ ही, हृदय रोग और कैंसर के लिए आप जिन दवाओं का सेवन करते हैं, वे thyroid hormone  के उत्पादन को कम कर सकते हैं, जो hypothyroidism का कारण बनता है।</p>

									<p class="mb-2"><b>6. गर्भावस्था</b></p>
									<p class="mb-2">गर्भावस्था के दौरान, महिलाएं बहुत अधिक मात्रा में thyroid hormone  का उत्पादन करती हैं। यदि डॉक्टर इस स्थिति का इलाज नहीं करते हैं, तो इससे preeclampsia , समय से पहले प्रसव और miscarriage की संभावना बढ़ जाती है। प्रसव के बाद , hormones के  उत्पादन में गिरावट काफी तेजी से होती है, जो postpartum thyroiditis का कारण बनती है।</p>
									<p class="mb-3">ज्यादातर मामलों में, इस बीमारी के लक्षण 12 से 18 महीनों के भीतर गायब हो जाते हैं। हालांकि, ऐसी स्थितियां बन सकती हैं जो स्थायी hypothyroidism की ओर ले जाती है। गर्भावस्था hypothyroidism का कारण बन सकती है।</p>

									<p class="mb-2"><b>7. विकिरण चिकित्सा</b></p>
									<p class="mb-3">जब गर्दन या सिर पर कैंसर का पता चलता है, जैसे कि leukemia  या lymphoma, तो उपचार का सबसे सामान्य रूप radiation therapy है। हालांकि, इस उपचार का नकारात्मक पक्ष यह है कि यह thyroid hormone  के उत्पादन को रोक या धीमा करके आपकी thyroid gland को प्रभावित करता है। उपचार के अंत में, व्यक्ति के hypothyroidism से प्रभावित होने की संभावना होती है।</p>
									
									<p class="mb-2"><b>8. सर्जरी</b></p>
									<p class="mb-2">Thyroid gland से जुड़ी समस्याओं के मामले में, चिकित्सक भावी जटिलताओं को रोकने के लिए कई बार ग्रंथि को हटाने का विकल्प चुन सकता है। इसके कारण व्यक्ति hypothyroidism से पीड़ित हो सकता है, जिससे दवा का सेवन अनिवार्य हो जाता है।

									<p class="mb-3">हालांकि, अगर सर्जरी के दौरान आपके थायरॉयड ग्रंथि का केवल एक छोटा हिस्सा हटा दिया गया था, तो फिर भी कुछ hormone  उत्पादन की संभावना बची रह सकती है।</p>

									<p class="mb-3"><b>Hypothyroidism</b> के मामले में सुझाए गए आहार संशोधन</p>
									
									<p class="mb-2"><b>A.</b> अनाज, और दालें</p>
									<p class="mb-3">भारतीय अनाज और दालें जैसे बाजरा, बंगाल चना, किडनी बीन्स, चना दाल, मसूर दाल, मूंग दाल, साबुत काले चने, परवल चावल, और ज्वार नाश्ते, दोपहर के भोजन और रात के खाने के लिए अच्छे विकल्प हैं। goitrogens, जैसे कि gluten युक्त अनाजों को आहार में कम करना महत्वपूर्ण है। इनमें गेहूं, जौ और राई शामिल हैं।</p>

									<p class="mb-2"><b>B.</b> सब्जियां</p>
									<p class="mb-2">Hypothyroidism से छुटकारा पाने के सर्वोत्तम तरीकों में से एक आपके आहार में विटामिन ए का सेवन बढ़ाना है। आप इसे अपने आहार में पीली और नारंगी सब्जियों जैसे खाद्य पदार्थों को शामिल करके कर सकते हैं। हरी बीन्स, बेल पेपर, बैंगन, प्याज, आलू, भिंडी, टमाटर, कद्दू, गाजर, avocado, अजवाइन, mushroom, खीरा, और समुद्री शैवाल hypothyroidism के लोगों के लिए बेहतर खाद्य पदार्थ हैं।</p>

									<p class="mb-3">Goitrogens वाली सब्जियों को कड़ाई से आहार से हटाया जाना चाहिए। गोभी, broccoli, फूलगोभी, शलजम, सरसों, मूली, सोया, brussels sprouts, और पालक जैसी सब्जियों को आहार में कम से कम किया जाना चाहिए। इन खाद्य पदार्थों का सेवन करते समय, उन्हें भाप देने या पकाने से goitrogenic compounds कम हो सकते हैं।</p>

									<p class="mb-2"><b>C.</b> फल</p>
									<p class="mb-2">आड़ू, strawberries और नाशपाती को goitrogenic कहा जाता है। इन फलों का सेवन प्रतिबंधित करना बेहतर है।</p>
									<p class="mb-3">Blueberries, blackberries, raspberries, सेब, केले, cantaloupe, cherries, खट्टे फल, cranberries, आम, अनानास, अनार, और कीवी फल Hypothyroidism वाले लोगों के लिए अच्छे फल हैं।</p>

									<p class="mb-2"><b>D.</b>नट और बीज</p>
									<p class="mb-2">अखरोट, मूंगफली, कद्दू के बीज और flax seeds को goitrogenic कहा जाता है। उनके सेवन को प्रतिबंधित करना या लेने से पहले उन्हें भूनना बेहतर है।</p>
									<p class="mb-3">अन्य नट और बीज जैसे Brazil nuts, macadamia nuts, Hazelnuts और सूरजमुखी के बीज hypothyroidism वाले लोगों के लिए अच्छे हैं।</p>

									<p class="mb-2"><b>E.</b>पशु उत्पाद</p>									
									<p class="mb-3">Seafood, free-range poultry और hormone -रहित मांस उत्पाद अच्छे विकल्प हैं। दूध, पनीर, दही, और श्रीखंड जैसे डेयरी उत्पादों का उचित मात्रा में सेवन करना चाहिए ताकि विटामिन डी की कमी न हो जो Hashimoto’s disease से सम्बद्ध होता है ।</p>

									<p class="mb-2"><b>F.</b>मसाले और जड़ी बूटी</p>									
									<p class="mb-3">लहसुन, प्याज, मिर्च, दालचीनी, हल्दी, काली मिर्च, ऋषि, अजवायन, अदरक, और oregano  आपके metabolic rate को बढ़ाने के लिए उपयोगी होंगे । अपने आहार में इनका उपयोग करना hypothyroidism में अच्छा है।</p>

									<p class="mb-2"><b>G.</b>तेल और वसा</p>									
									<p class="mb-3">Hypothyroidism से पीड़ित लोगों के लिए जैतून का तेल, और नारियल का तेल फायदेमंद है। खराब वसा और तले हुए खाद्य पदार्थों में कटौती करने से वजन कम करने में मदद मिलती है।</p>									
									<p class="mb-2"><b>H.</b>सोया आधारित खाद्य पदार्थ</p>									
									<p class="mb-3">सोया आधारित खाद्य पदार्थ जैसे tofu, tempeh, edamame, soya milk प्रतिबंधित होना चाहिए।</p>						

									<p class="mb-2"><b>I.</b>Processed Foods</p>									
									<p class="mb-3">Processed Foods में calorie बहुत होती हैं और पोषण बहुत कम । ये खाद्य पदार्थ वजन बढ़ाने की प्रवृत्ति को बढ़ावा देते हैं, और hypothyroidism के मामले में इनसे बचा जाना चाहिए। cookies, cakes and doughnuts जैसे processed foods अपने से दूर रखिए ।</p>												

									<p class="mb-2">Thyroid भोजन योजना में निम्नलिखित अवयव - समृद्ध खाद्य पदार्थ होने चाहिए:</p>
									<p class="mb-3">Iodine - जैसे अंडे / आलू / टूना आदि।
									<p class="pl-3 mb-2">Selenium- जैसे brazil nuts/chia seeds/ brown rice आदि।</p>	
									<p class="pl-3 mb-2">Zinc - जैसे lentils, दही, भिंडी आदि।</p>	
									<p class="pl-3 mb-2">विटामिन बी- जैसे उबले अंडे, avocado, दलिया आदि।</p>	
									<p class="pl-3 mb-2">विटामिन सी- जैसे अमरूद, आंवला, खट्टे फल आदि।</p>	
									<p class="pl-3 mb-2">विटामिन ई- जैसे टमाटर, bell peppers, नारियल तेल।</p>																
									<p class="mb-3">इनके सेवन की मात्रा को समायोजित करने के लिए मधुमेह, उच्च रक्तचाप, उच्च cholesterol और हृदय रोग जैसी अन्य चिकित्सा स्थितियों को ध्यान में रखना चाहिए।</p>

									<p class="mb-3">यदि आपको hypothyroidism है, तो इसका मतलब यह नहीं है कि आपको स्वादिष्ट खाद्य पदार्थों में पूरी तरह से कटौती करनी होगी। क्या? कितना खाना है?यह नियंत्रण में रह कर सुनिश्चित करना होगा। साथ ही  पर्याप्त शारीरिक गतिविधि  बनाए रखना  और तनाव मुक्त रहना भी hypothyroidism प्रबंधन के महत्वपूर्ण पहलू हैं। </p>

									<p class="mb-3"><b>Goitrogens</b> के प्रभावों को कम करने के लिए, आप निम्नलिखित उपायों का विकल्प चुन सकते हैं:</p>					
									
									<p class="mb-2"><b>1</b> यदि आपका thyroid  कम सक्रिय है, या आप अपने आहार में goitrogens को लेकर चिंतित हैं, तो नकारात्मक प्रभावों के जोखिम को कम करने के कुछ सरल तरीके नीचे सुझाए गए हैं:</p>						
									<p class="mb-2">अपने आहार को परिवर्तित करते रहिए: विभिन्न प्रकार के पादप खाद्य पदार्थों को खाने से आपको goitrogens की मात्रा को सीमित करने में मदद मिलेगी। इसके अलावा, आपको पर्याप्त vitamin और minerals प्राप्त करने में भी मदद मिलेगी।</p>				
