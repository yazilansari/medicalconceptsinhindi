									<p class="mb-3">किसी नवजात शिशु में hypoglycemia, prolonged jaundice, microphallus या midline facial abnormalities का होना या traumatic delivery की history मिलना तथा किसी बच्चे में head injury, CNS infection, CNS irradiation, consanguinity की history मिलना अथवा परिवार में किसी अन्य में GHD का होना इस सम्भावना को बढ़ा देता है कि short stature से प्रभावित उस बच्चे में growth hormone deficiency हो। </p>
									
									<p class="mb-2"><b>Clinical features</b></p>

									<p class="mb-2">सामान्यतयः सभी माता-पिता अपने बच्चे को लम्बा एवं स्वस्थ देखना चाहते हैं। अक्सर उनके अनुसार उनका बच्चा ठीक प्रकार से बढ़ता हुआ प्रतीत नहीं होता। इसीलिए, यह सुनिश्चित करने की आवश्यकता होती है कि बच्चा वास्तव में short है अथवा नहीं। इसके लिए सर्वप्रथम बच्चे की height ठीक प्रकार से नापनी चाहिए एवं इसकी तुलना उसी socioeconomic group के अन्य बच्चों के height chart से करनी चाहिए। Height के 3rd centile से कम होने पर ही इसे short stature माना जाता है जिसके लिए अन्य जांचों की आवश्यक पड़ती है। कभी-कभी केवल एक बार नापी गयी height से ही यह अनुमान लगा पाना संभव नहीं होता कि यह normal है अथवा abnormal। ऐसे में 6 माह तक बच्चे की height नापकर उसकी growth velocity (GV) का आकलन किया जाता है। GV का भी 25th centile से नीचे रहना, short stature की diagnosis को सुनिश्चित करता है।</p>									
									<p class="mb-2">Short stature अनेकों कारणों से उत्पन्न हो सकता है। परन्तु growth hormone deficiency (GHD) से होने वाले short stature के कुछ विशिष्ट लक्षण हो सकते हैं। किसी नवजात शिशु में hypoglycemia, prolonged jaundice, microphallus या midline facial defects का होना या traumatic delivery की history मिलना तथा किसी बच्चे में head injury, CNS infection, CNS irradiation, consanguity की history मिलना अथवा परिवार में किसी अन्य में GHD का होना इस सम्भावना को बढ़ा देता है कि short stature का कारण hypopituitarism अथवा GHD हो। </p>									
									<p class="mb-2">GHD से प्रभावित बड़े बच्चों में height gain की तुलना में weight gain अधिक होता रहता है जिसके कारण उनमें subcutaneous fat की मात्रा अधिक दिखाई देती है। इसे चेहरे पर <b>cherubic appearance</b> एवं पेट पर fat के घेरे बन जाने के कारण <b>ripply abdominal fat</b> कहते हैं। ध्यान रहे, height की तुलना में weight का अधिक होना hypothyroidism में भी मिलता है।</p>																		
