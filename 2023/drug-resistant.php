									<p class="mb-3"><b>Spread</b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> T.B का एक मरीज यदि वर्ष भर इलाज रहित रह जाए तो 10 से 12 व्यक्तियों को संक्रमित कर सकता है।</p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> TB का मरीज खांसते छींकते और बात करते वक्त इस बीमारी को सूक्ष्म aerosols के द्वारा फैलाता है।  </p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> कमजोर प्रतिकरक शक्ति वाले व्यक्ति को यह बीमारी अतिशीघ्र ग्रसित करती है। </p>
									

									<p class="mb-3"><b>Investigations </b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pulmonary TB  की मुख्य जांच बलगम से होती है। बलगम ना बनने पर hypertonic saline या bronchoalveolar lavage से निकाला जाता है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इस टेस्ट से बिमारी के पकड़ में आने की संभावना सिर्फ 50 प्रतिशत है और इसमें cases छूटने का बहुत डर होता है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसलिए जांच 1000 microscopic fields की जांच कर उसके बाद ही negative दर्शाया जाता है।</p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Auramine, Auramine Rhodamine O ये दो विशेष स्टेन led fluorescent सूक्ष्मदर्शी परीक्षण में उपयोग में लाए जाते हैं। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> TB की confirmatory जांच के लिए culture कराना आवश्यक है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Lowenstein Jensen broth Culture जैसी जांच में 4 से 8 हफ्तों में रिपोर्ट आती है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Middlebrook में 3 से 4 हफ्तों में, Liquid Culture अथवा HPLC में 1 से 2 हफ्ते में रिपोर्ट आती है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Drug Resistance के लिए line probe assay बहुत ही महत्व पूर्ण जांच है। </p>


