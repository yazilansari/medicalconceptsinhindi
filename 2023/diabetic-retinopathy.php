									<h5>Diabetic retinopathy</h5>	
									<!-- <p>Spirometry - Method And Interpretation</p> -->
									
									<!-- <p class="mb-2"><b>Lung Function Tests (Part 1)</b></p> -->
									<p class="mb-3">एक अनुमान के अनुसार विश्व में लगभग 20 करोड़ लोग डायबिटीज से प्रभावित हैं जिनमें से लगभग 4 करोड़ भारतीय हैं। इस संख्या का आगामी2030 तक लगभग 8 करोड़ तक पहुँच सकने का अनुमान है। क्या तुम जानते हो कि डायबिटीज में होने वाली अधिकाँश परेशानियां बढ़ी हुई शुगर से नहोकर vascular occlusion से होती हैं? Large arteries के block होने (macroangiopathy) से ischemic heart disease, ischemic cerebrovascular accidents एवं peripheral vascular disorders होते हैं जबकि small arteries के block होने (microangiopathy) सेnephropathy एवं retinopathy हो सकती है। आओ इस diabetic retinopathy (DR) के विषय में विस्तार से समझते हैं। </p>

									<p class="mb-2 mt-3"><b>Risk factors</b></p>									
									<p class="mb-2">• Duration of diabetes - Diabetes की अवधि जितनी अधिक होती जाती है, retina पर पड़ने वाला इसका दुष्प्रभाव उतना अधिक बढ़ताजाता है। 10 वर्ष से अधिक पुरानी type 1 diabetes में DR की prevalence 20% एवं type 2 diabetes में 25% तक बढ़ जाती है। 20 वर्षके बाद तो यह prevalence क्रमशः 90% एवं 60% तक बढ़ जाती है।</p>
									<p class="mb-2">• Severity of hyperglycemia - वास्तव में retina को damage तो hyperglycemia से ही होता है। Diabetes पुरानी हो जाने पर भी यदिभली-भांति नियंत्रण में हो तब उसके अन्य longterm complications की ही भांति DR की prevalence भी कम ही रह जाती है जबकिuncontrolled hyperglycemia इसकी संभावना को बढ़ता जाता है।</p>									
									<p class="mb-2">• Hypertension - तुम जानते ही हो कि systemic hypertension अपने आप में भी retinopathy उत्पन्न करता है। अतः स्वाभाविक ही है कियदि किसी व्यक्ति में uncontrolled hyperglycemia के साथ-साथ uncontrolled hypertension भी हो तब वह दोनों परस्पर मिलकरretinopathy को और अधिक तीव्रता से बढ़ायेंगें।</p>									
									<p class="mb-2">• Pregnancy - वास्तव में pregnancy के समय शरीर में अनेकों प्रकार के circulatory changes भी होते हैं जो DR की संभावना को और भीअधिक बढ़ा देते हैं।</p>									
									<p class="mb-2">• Heredity - अधिकाँश lifestyle disorders की ही भांति ही किसी एक व्यक्ति में DR होने पर उसके blood relatives में DR की संभावना भीबढ़ जाती है।</p>									
									<p class="mb-2">• Age - अत्यंत कम उम्र, जैसे 1-2 वर्ष, में ही diabetes हो जाने पर इसके 10 वर्ष हो जाने पर भी DR की सम्भावना नगण्य ही रहती है।</p>									
									<p class="mb-2">• Gender - Males एवं females, दोनों में, DR की सम्भावना लगभग बराबर ही होती है।</p>									

									<p class="mb-2 mt-3"><b>Diabetic retinopathy</b></p>									
									<p class="mb-2">DR में होने वाले परिवर्तनों को हम 4 प्रमुख भागों में बाँट सकते हैं।</p>		
									<p class="mb-2"><b>1.</b> Non-proliferative diabetic retinopathy (NPDR) - इसकी भी 4 stages हो सकती हैं।</p>		
									<p class="mb-2 pl-4"><b>a.</b> Mild NPDR, b) Moderate NPDR, c) Severe NPDR एवं d) Very severe NPDR</p>		
									<p class="mb-2"><b>2.</b> Proliferative DR</p>		
									<p class="mb-2"><b>3.</b> Diabetic maculopathy</p>		
									<p class="mb-2"><b>4.</b> Advanced diabetic eye disease (ADED)</p>		
									<p class="mb-2">आओ अब इनको क्रमशः विस्तार से समझते हैं।<br></p>		
									<p class="mb-2">Non-proliferative diabetic retinopathy (NPDR)</p>		
									<p class="mb-2">यह DR की प्रारम्भिक अवस्था है जिसमें retina में निम्नांकित लक्षण मिल सकते हैं।</p>		

									<p class="mb-2">• Hemorrhagic spots - Retinal capillary blockage के कारण block के distal part में ischemia उत्पन्न हो जाता है जिससे उस भाग कीcapillary wall भी ischemia से damage हो जाती है। इस भाग से blood रिस-रिस कर retina में hemorrhagic spots के रूप में दिखलाईपड़ता है। यदि यह रिसाव retina की deeper layers में होता है तब यह एक circumscribed round spot के रूप में दिखलाई पड़ता है। इसेdot अथवा blot hemorrhage कहते हैं। Retina की superficial layers में होने वाला यह रिसाव, retinal layers के बीच में फैलने लगता हैजो flame shaped hemorrhage के रूप में दिखलाई देता है।</p>									
									<p class="mb-2">• Exudates - Capillary leakage से जो proteins एवं lipids, retina की विभिन्न layers के बीच में पहुँच जाते हैं, वे white-yellow spotsके रूप में दिखलाई पड़ते हैं। इन्हें hard exudates कहते हैं। यह retina में कहीं भी हो सकते हैं, यहाँ तक कि इसके सर्वाधिक महत्वपूर्णmacular area में भी।</p>									
									<p class="mb-2">• Retinal vascular blockade से retinal nerve fibers के axoplasmic flow में भी बाधा आती है। यह रुका हुआ axoplasm, retina कीnerve fiber layer में nerve fibers के साथ-साथ फैलता हुआ किसी रुई के फाहे की भांति दिखता है। इन्हें soft exudates या cotton wool spots कहते हैं।</p>									
									<p class="mb-2">• Retinal edema - Blood, protein एवं lipids के रिसाव से retina में edema उत्पन्न होने लग जाता है जिससे retinal thickness भी बढ़जाती है।</p>									
									<p class="mb-2">• Intra-retinal microvascular abnormalities (IRMA) - Capillary blockade एवं neovascularisation के कारण retinal vasculature में कुछ structural changes भी देखने को मिलते हैं। यह कहलाती हैं। यह 2 प्रकार की हो सकती हैं।</p>									
									<p class="mb-2">• Venous abnormalities - Veins बीच-बीच में फूल जाती हैं (venous dilatation) जो venous beading के रूप में दिखती है। कहीं-कहींया पूरी vein ही फ़ैल कर venous loop बना लेती है।</p>
									<p class="mb-2">• Shunt channels - Capillary blockade के कारण retinal artery एवं vein के मध्य blocked circulation को पुनः आरम्भ करने के लिएइनके मध्य inactive पड़ी हुई shunt channels खुलने लगती हैं। यह red lines के रूप में दिखती हैं।</p>									
									
									<p class="mb-3">Retina में उपरोक्त लक्षणों की severity के आधार पर NPDR को 4 stages में बांटा जा सकता है।</p>		
									<p class="mb-2">• Mild NPDR - जब कम से कम एक microaneurysm, red spot के रूप में दिखाई पड़ रहा हो।</p>									
									<p class="mb-2">• Moderate NPDR - Mild NPDR+</p>									
									<p class="mb-2">• जब microaneurysms retina के 2 या 3 quadrants में हों</p>									
									<p class="mb-2">• intraretinal hemorrhages and/or cotton wool spots हों</p>									
									<p class="mb-2">• early mild IRMA दिखलाई पड़ रही हों।</p>									
									<p class="mb-2">• Severe NPDR - Rule of 4/2/1</p>									
									<p class="mb-2">• जब micro-aneurysms अथवा intraretinal hemorrhages, retina के चारों quadrants में मिल रहे हों (4)</p>									
									<p class="mb-2">• Venous beading, retina के 2 quadrants में हों (2)</p>								
									<p class="mb-2">• IRMA, retina के कम से कम 1 quadrant में हो (1)</p>								
									<p class="mb-2">• Very severe NPDR</p>									
									<p class="mb-2">• जब severe NPDR के 2 या 2 से अधिक लक्षण दिख रहे हों</p>									
									<p class="mb-2">• यह stage ही proliferative DR की और बढ़ते हुए blindness का कारण बनती है।</p>									
									
									<p class="mb-3">Proliferative DR</p>		
									<p class="mb-2">• Neovascularisation - BDR की stage तक retinal vasculature में दो प्रकार के परिवर्तन उत्पन्न होने लगे थे - permeability बढ़ जाने सेhemorrhages एवं exudates तथा veins एवं capillaries में कुछ structural changes । इनके पश्चात् जब capillary blockade से retinal ischemia एवं hypoxia उत्पन्न होता है तब उससे विभिन्न प्रकार के growth factors के बनने से retina में neovascularisation आरम्भ होजाता है। इस stage को PDR कहते हैं।</p>									
									<p class="mb-2">• Neovascularisation at disc (NVD) - जब यह neovascularisation (NV), optic disc पर हो रहा हो।</p>									
									<p class="mb-2">• Neovascularisation elsewhere (NVE) - जब यह NV, optic disc के अतिरिक्त, retina के अन्य स्थानों पर हो रहा हो।</p>									
									<p class="mb-2">• Hemorrhages - यह neovascularisation, retina की surface से बढ़ता हुआ धीरे-धीरे vitreous humour में पहुँचने लगता है। क्योंकि यहneovascularisation, normal vessels के विपरीत fragile एवं कमजोर होता है अतः यह कभी भी फट कर bleeding उत्पन्न कर सकता है। Retinaके anteriorly होने वाली यह bleeding, light rays को retina तक पहुँचने से रोक कर localised blindness उत्पन्न करती है।</p>									
									<p class="mb-2">• Pre-retinal hemorrhage - यदि यह hemorrhage, retina की internal limiting membrane एवं posterior hyloid membrane केमध्य हो तब इसे preretinal hemorrhage कहते हैं। इसकी विशेषता यह है कि आगे-पीछे दोनों ओर से encapsulated यह blood clot नहींकरता। Blood cells के settle कर जाने के कारण इसका ऊपरी भाग एक straight line के रूप में दिखलाई पड़ता है जो posture बदलने केसाथ-साथ readjust होता रहता है।</p>									
									<p class="mb-2">• Vitreous hemorrhage - यदि यही neovascularisation, vitreous humour तक पहुँच जाए एवं वहां rupture होकर bleeding उत्पन्नकरे तब यह vitreous hemorrhage कहलाता है।</p>									
									<p class="mb-2">• Retinal detachment - Retina से vitreous में होने वाला यह vitreous hemorrhage, धीरे-धीरे organise होकर fibrous tissue में बदलजाता है जो retina एवं vitreous के मध्य fibrous bands के रूप में दीखते हैं।</p>									
									<p class="mb-2">• यही fibrous bands जब contract करते हैं तब वह retina पर खिंचाव (traction) डालते हैं जिसके कारण retina अपने base (choroid) सेउखड जाता है। इसे retinal detachment (RD) कहते हैं। क्योंकि यह RD, retina पर पड़ने वाले traction से उत्पन्न होता है इसलिए इसेtraction retinal detachment भी कहते हैं।</p>									
									<p class="mb-2">• इस RD से retina में स्थान-स्थान पर cracks अथवा holes उत्पन्न हो सकते हैं। इन cracks अथवा holes से जब retinal layers के बीच मेंfluid प्रवेश करने लगता है तब वह पुनः retina को इसके base से हटाता है। इस प्रकार बढ़ रहा retinal detachment, secondary retinal detachment कहलाता है।</p>									
									<p class="mb-2">• Stages of PDR</p>									
									<p class="mb-2">• PDR without high risk changes - यदि NVE or NVD 1/2 disc diameter से छोटे हों</p>									
									<p class="mb-2">• PDR with high risk changes - यदि NVD or NVE 1/2 disc diameter से बड़े हों या pre-retinal and/or vitreous hemorrhage केसाथ हों</p>									
									
									<p class="mb-3">Diabetic maculopathy</p>									
									<p class="mb-3">DR की किसी भी अवस्था में जब retina का macular area प्रभावित होने लगता है तब इसे maculopathy कहते हैं। जिन-जिन retinal lesions कोइस category में रखा जाता है वह निम्नांकित हैं -</p>									
									<p class="mb-2">• Retinal thickening - यदि वह center of fovea के 500 Micro-m के अंदर है।</p>									
									<p class="mb-2">• Hard exudate - यदि वह center of fovea के 500 Micro-m के अंदर है एवं इसके पास की retina में retinal thickening भी मिल रही हो।</p>									
									<p class="mb-2">• Retinal thickening - यदि उसका आकार 1 disc area से अधिक हो एवं वह center of macula के 1 disc diameter के अंदर हो</p>									
									
									<p class="mb-3">• Diabetic maculopathy 3 प्रकार की हो सकती है।</p>									
									<p class="mb-2">• Focal exudative maculopathy - इसमें microaneurysm, hemorrhages, well circumscribed macular edema एवं hard exudates एक circinate pattern में macular region में एक ही स्थान पर मिलते हैं। Retina की Henle's layer में radially arranged होनेके कारण इन्हें macular star कहते हैं। Fluorescein angiography में इस स्थिति में macular perfusion तो पर्याप्त मिलता है जिसमें बीच-बीच में focal leakages मिल सकते हैं।</p>									
									<p class="mb-2">• Diffuse exudative maculopahty - Focal exudative maculopathy में मिलने वाला macular edema जब well circumscibed न रहकर retina के posterior pole पर एक बड़े area में diffusely present हो, जिसमें कुछ hard exudates भी हों, तब वह स्थिति diffuse exudative maculopahty कहलाती है। Fluoresceine angiography में इस स्थिति में retina के posterior pole में diffuse leakages मिलते हैं।</p>									
									<p class="mb-2">• Ischemic maculopathy - इस स्थिति में microaneurysms, macular edema एवं hard exudates तो मिलते ही हैं परन्तु साथ हीmicrovascular occlusion के कारण से macular ischemia भी आरम्भ हो जाता है। तुम जानते हो कि retina में macula ही वह area हैजहाँ cones की संख्या सर्वाधिक होती है जिससे वहां से सबसे clear दिखलाई पड़ता है। इस area में ischemia होने से total vision loss तकहो सकता है। Fluorescein angiography में इस स्थिति में macular perfusion लगभग समाप्त हो चुका होता है।</p>									

