									<h5>Metabolic Actions Of Growth Hormone</h5>
									<p class="mb-3">Metabolic Actions Of Growth Hormone</p>								
									<p class="mb-3"><b>Growth hormone</b></p>								
									<p class="mb-3">GH के protein एवं glucose sparing actions के कारण अपनी energy requirements को पूरा करने के लिए शरीर की निर्भरता fat पर बढ़ती जाती है। यह मुख्यतः दो प्रकार से पूरी होती है। </p>

									<p class="mb-3 dotedt2"><span>1.</span> Adipose tissues में lipolysis के द्वारा fatty acids का production बढ़ाना जिससे energy substrate के रूप में नया fuel प्राप्त हो सके।  </p>
									<p class="mb-3 dotedt2"><span>2.</span> Peripheral cells में उपरोक्त fatty acids के oxidation से energy प्राप्त करना। </p>

									<p class="mb-3">ध्यान रहे, anterior pituitary के सभी hormones मुख्यतः अपनी-अपनी target glands पर सीधे ही कार्य करते हैं परन्तु GH का अपनी कोई विशिष्ट target नहीं होता। इसका प्रभाव लगभग सभी body tissues पर पड़ता है। वास्तव में GH ही जन्म के पश्चात् होने वाली postnatal growth (natal=birth) के लिए सर्वाधिक महत्वपूर्ण hormone है। इसीलिए इसे somatotropic hormone (soma=body) या somatotropin भी कहते हैं। शरीर के जिन भी tissues में grow करने की क्षमता होती है उनमें GH के प्रभाव से multiplication (cell number का बढ़ना), differentiation (immature cell का किसी एक दिशा में maturation) एवं growth (cell size का बढ़ना) इत्यादि लक्षण उत्पन्न होने लगते हैं। स्वाभाविक रूप से इस कार्य के लिए proteins की अधिक मात्रा में आवश्यकता पड़ेगी जिसके लिए GH, protein synthesis को बढ़ाता है। परन्तु growing tissues को proteins के साथ-साथ energy substrate के रूप में glucose की भी आवश्यकता पड़ती है। इसके लिए GH, peripheral cells में glucose का utilisation घटा देता है एवं इन cells में energy substrate के रूप में fat utilisation को प्रोत्साहित करता है। </p>


									<p class="mb-3">इस प्रकार, GH के मुख्य <b>metabolic effects</b> कुछ इस प्रकार हुए </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> 
									Increase in rate of protein synthesis</p>

									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i>  
									Decrease in rate of glucose utilisation </p>
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i>  Increase in rate of fat utilisation for energy </p>									
									
									<p class="mb-4">आओ इनको विस्तार से समझते हैं। </p>

									<p class="mb-3"><b>Increase in rate of protein synthesis</b> </p>
									<p class="mb-2">Protein synthesis को बढ़ाने के लिए GH के actions को protein synthesis के steps द्वारा ही समझा जा सकता है। </p>
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> सर्वप्रथम, nuclear DNA पर <b>transcription</b> के द्वारा RNA का निर्माण होता है। GH के प्रभाव में यह बढ़ जाता है। वास्तव में long term में यही GH का सर्वाधिक महत्वपूर्ण कार्य है।</p>
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> इसके पश्चात्, cytoplasmic ribosomes पर <b>translation</b> के द्वारा इस RNA से protein की synthesis होती है। GH के प्रभाव में यह भी <b>बढ़ जाता है।</b></p>
									<p class="mb-2 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> क्योंकि new protein synthesis के लिए amino acids की आवश्यकता पड़ेगी अतः GH cell membrane से <b>amino acids</b> का <b>transport बढ़ा देता है</b> जिससे protein synthesis के लिए cell में पर्याप्त raw material उपलब्ध हो सके।</p>
									<p class="mb-4 dotedt"> <i class="fa fa-long-arrow-right" aria-hidden="true"></i> इसके अतिरिक्त, यदि शरीर की energy requirement के लिए protein को gluconeogenesis के substrate के रूप में प्रयोग में लाया जाता रहेगा तब भी शरीर में proteins की मात्रा नहीं बढ़ सकेगी। इसलिए GH, cells में protein एवं amino acids की catabolism को घटाता है। इसे GH का <b>protein sparing action</b> कहते हैं।</p>

									<p class="mb-3"><b>Decrease in rate of glucose utilisation </b></p>
									<p class="mb-2">तुम जानते हो कि शरीर के लगभग सभी hormones, insulin के विरुद्ध कार्य करते हैं अर्थात antiinsulin होते हैं एवं blood में glucose का स्तर बढ़ाते हैं। GH भी इसका अपवाद नहीं है। शरीर में मुख्यतः तीन insulin sensitive tissues होते हैं, liver, muscles एवं adipose tissues GH इन तीनों tissues पर insulin का प्रभाव घटा देता है। इसके निम्नांकित परिणाम होते हैं। </p>
									
									<p class="dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Liver में glycogenolysis एवं gluconeogenesis की प्रक्रिया बढ़ जाने से <b>glucose production बढ़ जाता है।</b></p>
									<p class="dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Muscles एवं adipose tissues में glucose uptake घट जाने से <b>glucose utilisation घट जाता है।</b></p>
									<p class="dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> इन दोनों प्रक्रियाओं से blood में glucose का स्तर बढ़ने लगता है <b>(hyperglycemia)</b> । यहाँ तक कि GH के excessive secretion से secondary diabetes तक उत्पन्न हो सकती है। </p>
									<p class="dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Insulin sensitive tissues पर insulin sensitivity के घट जाने (insulin resistance) से इस resistance को overcome करने के लिए insulin secretion   बढ़ने लगता है जिससे <b>compensatory hyperinsulinemia</b> उत्पन्न हो जाता है। GH द्वारा उत्पन्न यह hyperglycemia एवं hyperinsulinemia वास्तव में cell growth एवं replication में सहायक होते हैं।   </p>

									<p class="mb-3"><b>Increase in rate of fat utilisation for energy </b></p>
									<p class="mb-2">GH के metabolic actions के प्रभाव में body composition में भी अनेक प्रभाव देखे जा सकते हैं। </p>
									<p class="mb-2 dotedt2"><span>1.</span> Nitrogen retention एवं protein synthesis के बढ़ने से lean body mass या muscle mass बढ़ता है।  </p>
									<p class="mb-2 dotedt2"><span>2.</span> Lipolysis के बढ़ने से subcutaneous एवं omental fat mass घटता है।  </p>
									<p class="mb-2 dotedt2"><span>3.</span> इन्हीं प्रभावों के कारण, GH deficiency में growth retardation के अतिरिक्त muscle mass में कमी एवं subcutaneous fat की अधिकता  मिलती है। </p>
