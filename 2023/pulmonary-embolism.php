									<p class="mb-3">Answers & explanations</p>
									<p class="mb-2"><b>1. उत्तर -(a)</b> </p>
									<p class="mb-4">Pulmonary Embolism में  सबसे आम तौर पर  सांस फूलने की दिक्कत होती है । इसलिए जब अप्रत्यक्ष Pulmonary Embolism, pneumonia या congestive heart failure के साथ में होता है, तो मरीज में उचित इलाज के बाद भी सुधार नहीं दिख पाता है। ऐसे में हमें Pulmonary Embolism के साथ में होने की संभावना को ध्यान में रखना चाहिए। </p>

									<p class="mb-2"><b>2. उत्तर -(c)</b> </p>
									<p class="mb-4">जब Well's point स्कोर चार से ऊपर होता है तब PE होने की संभावना अत्यधिक बड़ जाती है । </p>

									<p class="mb-2"><b>3. उत्तर -(b)</b> </p>
									<p class="mb-4">Massive pulmonary embolism 5-10% मरीज़ों में मिलता  है। इसमें pulmonary vasculature के कम से कम 50% हिस्से में खून के थक्के जम जाते हैं व  इसके लक्षण हैं- सांस फूलना, चक्कर आना, रक्तचाप कम होना व शरीर का नीला पड़ना।  ऐसे मरीज़ों में cardiogenic shock व multiorgan failure के कारण मृत्यु भी हो सकती है।  </p>

									<p class="mb-2"><b>4. उत्तर-(c) Clubbing</b> </p>
									<p class="mb-3"></p>

									<p class="mb-2"><b>5. उतर-(d) Imaging </b></p>
									<p class="mb-4">Wells point स्कोर की मदद से हम मरीज़ में PE होने की संभावना का आंकलन कर सकते हैं। ऐसे मरीज़ जिनमे यह संभावना काम होती है उनमें हमें सबसे पहले d-dimer की जांच करवानी चाहिए उसके बड़े होने पर ही imaging करवानी चाहिए। हालाँकि जब मरीज़ में PE होने की संभावना अधिक हो तब हम सीधे ही इमेजिंग (CECT-chest )करवानी चाहिए। </p>

									<p class="mb-2"><b>6. उत्तर-(d) Small PE</b></p>
									<p class="mb-4">Small PE के मरीज़ों में अधिकतर  पल्मोनरी infarction भी हो जाता है। ऐसे में मरीज़ को काफी दर्द हो सकता है क्यों की थक्का छाती की सतह के पास  यानि की pleural nerves के ज्यादा समीप जाकर अटक जाता है।  </p>

									<p class="mb-2"><b>7. उत्तर-(b)</b></p>
									<p class="mb-4">Pulmonary Embolism के मरीजों में plasmin द्वारा fibrin के टूटने से रक्त में d-dimer के स्तर बड़ जाते हैं।PE में  D-dimer की sensitivity 95% से भी अधिक होती  है। इसलिए d-dimer का सामान्य स्तर Pulmonary Embolism के diagnosis  को खारिज करने में सहायक है। परन्तु, रक्त में d-dimer का स्तर, pulmonary embolism के लिए विशिष्ट नहीं होता है। क्यूनि इसके स्तर बहुत सी परिस्थितियों में बढ़ सकते हैं जैसे कि - myocardial infarction, pneumonia, sepsis, cancer, postoperative state, pregnancy के 2nd और 3rd trimester में।</p>

									<p class="mb-2"><b>8. उत्तर-(b)</b></p>
									<p class="mb-4">Pulmonary Embolism के लिए लंग स्कैन अब 2nd-line diagnostic test है। यह जाँच ऐसे मरीजों में की  जाती है, जो I.V Contrast dye नहीं बर्दाश्त कर पाते हैं।Perfusion scan में दिक्कत आने का मतलब है खून का प्रवाह या तो कम हो गया है या पूरी तरह से समाप्त हो गया है और यह Pulmonary Embolism की ओर संकेत करता है ।Ventilation scan में abnormality आना non ventilated lung की ओर संकेत करती है। यह Pulmonary Embolism के अलावा भी, जिन बीमारियों में perfusion defect मिलता है उनकी ओर इशारा करता है। जैसे की Asthma  एवं COPDअगर scan में दो या उससे ज्यादा perfusion defect मिलते हैं, पर वेंटिलेशन सामान्य है, तो Pulmonary Embolism की संभावना सर्वाधिक है। इसकी संभावना बहुत ही कम हो जाती है, जब scan पूरी तरह से सामान्य है।</p>

									<p class="mb-2"><b>9. उत्तर-(d)</b></p>
									<p class="mb-4">Massive PE  मरीज़ों में हमें  fibrinolytic therapy भी देनी चाहिए। सामान्य तौर पर दी जाने वाली रेजिमेन में हम 100mg  recombinant tPA (alteplase) को २ घंटे में i.v. देना है। जितना जल्दी हम thrombolysis करेंगे उतने ही अच्छे हमें परिणाम मिलेंगे। हालाँकि अगले 14 दिनों तक हम मरीज़ को यह उपचार दे सकते हैं। कुछ मरीज़ों में इससे होने वाली ब्लीडिंग की समस्या को कम करने के लिए हम 50mg यानि की आधी डोज़ भी दे सकते हैं। </p>

									<p class="mb-2"><b>10. उत्तर-(d)</b></p>
									<p class="mb-4">Warfarin से इलाज़ के दौरान अगर मरीज़ को कहीं से अत्यधिक खून बहने लगे तो सबसे उचित उपचार है Prothrombin complex concentrates. अगर खून ज्यादा नहीं बहरा है व मरीज़ का रक्तचाप सामन्य है तब हम सिर्फ FFP व i.v. विटामिन K की मदद से इलाज़ कर सकते हैं। पर अगर मरीज़ में कोई खून बहने की समस्या नहीं है केवल उसका PT-INR अत्यधिक बड़ रहा है तब हमें सिर्फ उसे केवल ओरल विटामिन K देना है। </p>
