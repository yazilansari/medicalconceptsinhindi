									<h5>कुशिंग सिंड्रोम (Cushing's Syndrome)</h5>
									<p class="mb-4">कुशिंग सिंड्रोम (Cushing's Syndrome)</p>								
									
									<p class="mb-3"><b>कुशिंग सिंड्रोम क्या होता है?</b></p>								
									<p class="mb-3">कुशिंग सिंड्रोम एक जटिल हार्मोनल स्थिति होती है। यह तब होता है, जब किसी व्यक्ति के शरीर में कोर्टिसोल हार्मोन (cortisol hormone) का स्तर बहुत अधिक बढ़ जाता है। इसका प्रभाव लगभग पूरे शरीर पर होता  हैं। यह एक गंभीर विकार है, जो घातक हो सकता है।</p>

									<p class="mb-3">इसके सबसे सामान्य लक्षण में शामिल हैं, -</p>

									<p class="mb-3 dotedt2"><span>1.</span> वजन बढ़ना</p>
									<p class="mb-3 dotedt2"><span>2.</span> त्वचा पर निशान या नील पड़ना,</p>
									<p class="mb-3 dotedt2"><span>3.</span> उच्च रक्तचाप,</p>
									<p class="mb-3 dotedt2"><span>4.</span> ऑस्टियोपोरोसिस (Osteoporosis), </p>
									<p class="mb-3 dotedt2"><span>5.</span> सुगर का बढ़ना,</p>
									<p class="mb-3 dotedt2"><span>6.</span> फूला हुआ चेहरा,</p>
									<p class="mb-3 dotedt2"><span>7.</span> मास्पसियो में कमजोरी और</p>
									<p class="mb-3 dotedt2"><span>8.</span> महिलाओं में मासिक धर्म में रुकावट ( oligomenorrhea or amenorrhea)।</p>


									<p class="mb-3">जिन लोगों में इसके होने का अधिक खतरा होता है उनमें शामिल है, जो किसी और बीमारी के लिए स्टेरॉयड ( steroid) दवाओं की खुराक लेते हैं जैसे कि अस्थमा या फिर जिनकी पिट्यूटरी ग्रंथि में ट्यूमर है। इन दोनों ही परिस्थितियों में कॉर्टिसोल नामक स्टेरॉयड बहुत अधिक मात्रा में मिलता है। </p>									
									
									<p class="mb-3">कुशिंग सिंड्रोम के उपचार के तहत शरीर में कोर्टिसोल निर्माण की मात्रा को सामान्य किया जाता है और लक्षणों को बेहतर किया जाता है। उपचार जितना जल्दी शुरू होता है, स्वस्थ होने की संभावना उतनी ही बढ़ जाती है।</p>

									<p class="mb-4">कुशिंग सिंड्रोम के प्रकार (Types of Cushing's Syndrome)</p>
									
									<p class="mb-3">कुशिंग सिंड्रोम के कितने प्रकार होते हैं?</p>
									<p class="mb-2 dotedt2"> <span>1.</span> <u>एक्जोजिनस कुशिंग सिंड्रोम</u> (<b>exogenous cushing's Syndrome</b>)-</p>

									<p class="mb-2">कुशिंग सिंड्रोम विकसित करने वाले कारण यदि शरीर के बाहर से आए हैं, तो इस स्थिति को एक्जोजिनियस कुशिंग सिड्रोम कहा जाता है।</p>
									<p class="mb-2">एक्जोजिनस कुशिंग सिंड्रोम कोर्टिकोस्टेरॉयड दवाओं की अत्यधिक मात्रा का सेवन करने के परिणाम से भी हो सकता है। कोर्टिकोस्टेरॉय दवाएं जैसे प्रेडनिसोन (Prednisone), डेक्सामेथासोन (Dexamethasone) और मेथिलप्रेडनीसोलोन (Methylprednisolone)।</p>
									<p class="mb-2">जिन लोगों को गठिया , लुपस , अस्थमा जैसी बीमारी है या जिनके शरीर का कोई अंग बदला गया हो, उनके लिए प्रभावी इलाज के लिए इन दवाओं की उच्च खुराक की जरूरत पड़ती है। इससे भी शरीर पर वैसे ही प्रभाव होते हैं जैसे कोर्टिसोल की मात्रा बढ़ने पर होते हैं।</p>
									<p class="mb-4">इंजेक्शन के रूप में दिए जाने वाले कोर्टिकोस्टेरॉयड का इस्तेमाल जोड़ो में दर्द , पीठ में दर्द  और बर्साइटिस के लिए किया जाता है। यह भी कुशिंग सिंड्रोम पैदा कर सकता है।</p>
									
									
									<p class="mb-2 dotedt2"> <span>1.</span> <u>एंडोजिनस कुशिंग सिंड्रोम</u> (<b>endogenous cushing syndrome</b>) -
									</p>

									<p class="mb-2">जब कुशिंग सिंड्रोम को विकसित करने वाले कारण शरीर के अंदर से ही पैदा होते हैं, तो उसे एंडोजिनस कुशिंग सिंड्रोम कहा जाता है। उदाहरण के लिए जैसे एड्रीनल ग्रंथि द्वारा अत्यधिक कोर्टिसोल पैदा करना।</p>
									<p class="mb-4">कई बार एक पिट्यूटरी ग्रंथि के ट्यूमर के कारण एड्रीनोकोर्टिकोट्रोपिक हार्मोन (ACTH) का अधिक उत्पादन होने लगता है , जीके कारण  कोर्टिसोल हार्मोन की मात्रा बढ़ जाती है। एड्रीनल ग्रंथियों के विकार, जैसे कि एक adrenal adenoma या adrenal cancer के चलते भी कोर्टिसोल का निर्माण बढ़ जाता है।</p>
									

									<p class="mb-3"><u>कुशिंग सिंड्रोम के लक्षण -</u></p>
									<p class="mb-3"><b>कुशिंग सिंड्रोम के लक्षण क्या होते हैं?</b></p>
									<p class="mb-2">कुशिंग सिंड्रोम के लक्षण और संकेत अलग-अलग हो सकते हैं।</p>
									<p class="mb-2"><b>सामान्य लक्षण:</b> जिनमें लगातार बढ़ता मोटापा और त्वचा के बदलाव शामिल हैं, जैसे:</p>
									
									<p class="mb-2 dotedt2"><span>1.</span> वजन बढ़ना और मांसल ऊतक इकट्ठा होना, विशेष रूप से पीठ के मध्य और ऊपरी हिस्से में, चेहरे पर, दोनों कंधों के बीच कमर में, जो कि एक कूबड़ की तरह नजर आने लगें। </p>
									<p class="mb-2 dotedt2"><span>2.</span> पेट, जांघों, स्तन और बाजूओं की त्वचा पर गुलाबी या हल्के स्ट्रेच के निशान (Striae) </p>
									<p class="mb-2 dotedt2"><span>3.</span> त्वचा का पतला और नाजुक पड़ जाना</p>
									<p class="mb-4 dotedt2"><span>4.</span> मुंहासे</p>
									
									<p class="mb-2"><b>कुशिंग रोग से ग्रसित महिलाएं:</b> जिनको निम्न समस्याएं हो सकती हैं:</p>
									<p class="mb-2 dotedt2"><span>1.</span>शरीर और चेहरे के बाल मोटे होना या अधिक उगना (Hirustism)</p>
									<p class="mb-4 dotedt2"><span>2.</span>मासिक धर्म में अनियमितता या बंद होना</p>
									
									<p class="mb-2"><b>कुशिंग रोग से ग्रसित पुरूष:</b> जिनको निम्न समस्याएं हो सकती हैं:</p>
									<p class="mb-2"><span>1.</span>कामेच्छा में कमी</p>
									<p class="mb-2"><span>2.</span>प्रजनन क्षमता में कमी</p>
									<p class="mb-4"><span>3.</span>स्तंभन दोष (Erectile dysfunction)</p>
									
									<p class="mb-2"><b>डॉक्टर को कब दिखाना चाहिए?</b></p>
									<p class="mb-4">अगर आप अस्थमा, गठिया या आंतों में सूजन जैसी बीमारियों के लिए कोर्टिकोस्टेरॉयड दवाएं ले रहे हैं और आपको ऐसे लक्षण व संकेत महसूस हो रहे हैं, जो कुशिंग सिंड्रोम का संकेत देते हैं तो डॉक्टर से बात करें। यहां तक कि अगर आप इन दवाओं का प्रयोग नहीं कर रहे हैं और आपमें एेसे लक्षण है जिनसे इन रोग के होने का संदेह होता है तो तुरंत अपने डॉक्टर से संपर्क करें।</p>

									<p class="mb-2">कुशिंग सिंड्रोम के कारण -</p>
									<p class="mb-3"><b>कुशिंग सिंड्रोम क्यों होता है?</b></p>
									<p class="mb-3"><b>एक्जोजिनस कुशिंग सिंड्रोम</b></p>
									<p class="mb-4">कुशिंग सिंड्रोम का सबसे सामान्य कारण कोर्टिकोस्टेरॉयड दवाओं (जैसे कि प्रेडनिसोन) की उच्च खुराक का लंबे समय तक इसका उपयोग करना होता है। इस तरह के कारणों से होने वाले कुशिंग सिंड्रोम को एक्जोजिनस कुशिंग सिंड्रोम कहा जाता है। अंदरूनी अंग प्रत्यारोपण की रोकथाम या बचाव करने के लिए भी डॉक्टर इन दवाओं को मरीज के लिए लिख सकते हैं। इन दवाओं का प्रयोग सूजन संबंधी रोग जैसे लुपस, गठिया आदि का इलाज करने के लिए किया जाता है। पीठ दर्द के लिए दिए जाने वाले स्टेयरॉय इंजेक्शन की अधिक खुराक भी कुशिंग सिंड्रोम का कारण बन सकती है।</p>
									<p class="mb-4">इनहेलेंट्स के रूप में स्टेरॉयड की कम खुराक, जैसे अस्थमा के लिए इस्तेमाल की जाने वाली दवा या eczema आदि के लिए प्रयोग की जाने वाली क्रीम आदि आम तौर पर कुशिंग सिंड्रोम को विकसित नहीं कर पाते।</p>

									<p class="mb-3"><b>एंडोजिनस कुशिंग सिंड्रोम</b></p>
									<p class="mb-4">एंडोजिनस कुशिंग सिंड्रोम आपके शरीर द्वारा कॉर्टिसोल के अधिक निर्माण करने के कारण होता है, जिसके कारण निम्न हो सकते हैं</p>
									
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> पिट्यूटरी ग्रंथि में ट्यूमर बहुत अधिक मात्रा में एड्रीनोकोर्टिकोट्रोपिक (Adrenocorticotropic) हॉर्मोन  जारी कर देता है, इसे कुशिंग के रोग (Cushing’s disease) भी कहा जाता है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> एक्टोपिक ACTH सिंड्रोम, जो आम तौर पर फेफड़े, अग्न्याशय, थायरॉयड, या थाइमस ग्रंथि में ट्यूमर के  कारण होता है।</p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> एड्रीनल ग्रंथि की असामान्यता या ट्यूमर।</p>
									
									<p class="mb-3">कुशिंग सिंड्रोम का निदान (Diagnosis of Cushing's Syndrome)</p>
									<p class="mb-2"><b>कुशिंग सिंड्रोम का परीक्षण कैसे किया जाता है?</b></p>
									<p class="mb-3">कुशिंग सिंड्रोम कई अलग-अलग कारणों से विकसित होता है। इसका निदान शरीर में कोर्टिसोल स्तर की असामान्यता के आधार पर किया जाता है। निदान के दौरान डॉक्टर शारीरिक जांच करते हैं और पिछली दवाओं और लक्षणों के बारे में जानकारी हासिल करने की कोशिश करते हैं। डॉक्टर कुछ लेबोरेट्री टेस्ट के ऑर्डर भी दे सकते हैं। जिनमें शामिल हैं,</p>
									
									<p class="mb-2">8 AM fasting cortisol – यह परिक्षण exogenous cushing syndrome की सम्भावना के बारे में पता करने के लिए बहुत उपयोगी है | Exogenous cushing syndrome मे  8 AM fasting cortisol का स्तर कम आता है |</p>
									<p class="mb-2">24 hour urinary free cortisol</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Midnight  salivary cortisol</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Overnight 1 mg dexamethasone suppression test</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Low-dose dexamethasone suppression test</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> High dose dexamethasone suppression test</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> corticotropin releasing hormone stimulation test</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Inferior petrosal sinus sampling(IPSS)</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pituitary MRI ( पिट्यूटरी ट्यूमर के संदेह की इस्तिथी में )</p>
									<p class="dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Adrenal MRI / CECT adrenal ( एड्रेनल  ट्यूमर के संदेह की इस्तिथी में ).</p>


									<p class="mb-2">कुशिंग सिंड्रोम का उपचार -</p>
									<p class="mb-2"><b>कुशिंग सिंड्रोम का उपचार कैसे किया जाता है?</b></p>
									<p class="mb-2">उपचार का मुख्य लक्ष्य कोर्टिसोल के बढ़े हुए स्तर को घटाना होता है, लेकिन उपचार के प्रकार कई कारकों पर निर्भर करते हैं। जिसमें सिंड्रोम के कारण भी शामिल होते हैं।</p>
									<p class="mb-2">अगर सिंड्रोम का कारण अस्थमा, गठिया या अन्य किसी स्थिति के इलाज के लिए कॉर्टिकोस्टिरॉइड का इस्तेमाल करना है। तो डॉक्टर इस दवा की खुराक को कम कर सकते हैं या इसको किसी अन्य नॉन-कोर्टिकोस्टेरॉयड दवाओं के साथ बदल सकते हैं।</p>
									<p class="mb-2">डॉक्टर की देखरेख के बिना मरीज को कोर्टिकोस्टेरॉयड दवाओं कि खुराक कम नहीं करना चाहिए, क्योंकि इसके कारण कोर्टिसोल स्तर में खतरनाक तरीके से कमी आ सकती है।</p>
									<p class="mb-2">ट्यूमर को सर्जरी की जरूरत पड़ सकती है, पिट्यूटरी ग्रंथि के ट्यूमर को रोगी की नाक के माध्यम से निकाला जाता है। एड्रीनल ग्रंथि, अग्न्याशय या फेफड़ों में ट्यूमर को नियमित सर्जरी या कीहोल सर्जरी की आवश्यकता हो सकती है।</p>
									<p class="mb-2">सर्जरी के बाद मरीज को कोर्टिसोल की वैकल्पिक दवाएं लेने की जरूरत पड़ती है, जब तक कि हार्मोन का उत्पादन सामान्य ना हो जाए। </p>
									<p class="mb-4">ट्यूमर को निकालने के लिए रेडियोथेरेपी का इस्तेमाल उपचार के एक भाग के रूप में किया जा सकता है। यदि ट्यूमर कैंसर ग्रस्त है, तो कीमोथेरेपी (Chemotherapy) भी अनिवार्य हो सकती है, उदाहरण के लिए फेफड़ो में कैंसर ग्रस्त ट्यूमर ।</p>


									<p class="mb-3">दवाएं जैसे कीटोकॉनेजॉल (ketoconazole), माइटोटैने (mitotane) और मेटीरापॉन (Metyrapone) आदि कोर्टिसोल के अधिक निर्माण को नियंत्रित करने में मदद करती हैं।</p>
									<p class="mb-3">यदि ट्यूमर के कारण हार्मोन निर्माण में कमी हुई है, तो डॉक्टर हार्मोन रिप्लेस्मेंट थेरेपी (HRT) का सुझाव दे सकते हैं।</p>
									<p class="mb-4">अगर कोई और उपचार काम ना करें तो, एड्रीनल ग्रंथि को सर्जरी  द्वारा निकालना भी पड़ सकता है</p>
									

									<p class="mb-3">कुशिंग सिंड्रोम के जोखिम और जटिलताएं -</p>
									<p class="mb-3"><b>कुशिंग सिंड्रोम में क्या जटिलताएं हो सकती हैं?</b></p>
									<p class="mb-2">अगर कुशिंग सिंड्रोम का शीघ्र उपचार ना किया जाए तो, निम्न जटिलताएं उत्पन्न हो सकती हैं:</p>
									
									<p class="mb-3 dotedt2"><span>1.</span> हड्डी मे क्षति (ऑस्टियोपोरोसिस), जिसके कारण हड्डी में असामान्य फ्रैक्चर हो सकता है।</p>
									<p class="mb-3 dotedt2"><span>2.</span> हाई बीपी (उच्च रक्तचाप)</p>
									<p class="mb-3 dotedt2"><span>3.</span> शुगर (डायबिटीज़)</p>
									<p class="mb-3 dotedt2"><span>4.</span> बार-बार होने वाला या असामान्य संक्रमण </p>
									<p class="mb-3 dotedt2"><span>5.</span> मांसपेशियों का कमजोर होना  आदि।</p>
									

									<p class="mb-3">कुशिंग सिंड्रोम में क्या खाना चाहिए?</p>
									<p class="mb-3"><b>कुशिंग सिंड्रोम होने पर क्या खाना चाहिए?</b></p>
									<p class="mb-3">स्वस्थ आहार कुशिंग के रोगियों के जीवन का एक बहुत जरूरी हिस्सा होता है।</p>
									<p class="mb-3">कैल्शियम  और विटामिन डी  वाले खाद्य पदार्थों का सेवन करके अपनी हड्डियों को बचाएं। </p>

