									<h5>Spirometry - Method And Interpretation</h5>	
									<p>Spirometry - Method And Interpretation</p>
									
									<p class="mb-2"><b>Lung Function Tests (Part 1)</b></p>
									<p class="mb-3">इन जांचों के माध्यम से रोगी की ventilatory capacity की माप की जाती है। इसका अर्थ हुआ कि breathing के समय lungs में कितनी वायु अंदर ली जा सकती है अथवा इसमें से कितनी वायु बाहर निकाली जा सकती है। यह गणना हम OPD अथवा IPD में कुछ clinical parameters के द्वारा भी कर सकते हैं एवं इसके लिए विशिष्ट मशीनों का प्रयोग भी किया जा सकता है। आइये इन्हें क्रमानुसार समझते हैं। </p>

									<p class="mb-2">Clinical parameters</p>									
									<p class="mb-2"><b>Duration of expiratory flow</b> - Stethoscope को trachea पर रख कर रोगी को कहें कि वह अपनी पूरी क्षमता से गहरी सांस लेकर फिर उसे तीव्रता से बाहर निकाले। सामान्यतयः इसमें 4 सेकण्ड से कम समय लगता है। इससे अधिक समय लगने का अर्थ है कि airway में कहीं बाधा उत्पन्न हो रही है। यह obstructive lung disease का लक्षण हो सकता है। </p>
									
									<p class="mb-4"><b>Breath holding test</b> - रोगी को कहें कि वह अपनी पूरी क्षमता से गहरी सांस लेकर उसे छोड़ते हुए गिनती गिने। सामान्यतयः यह गिनती 40 तक गिनी जा सकती है। इससे कम होने  पर आपके फेफड़े की क्षमता (lung capacity) में कमी हो सकती है</p>


									<p class="mb-4"> <b>Sneider test या blow a candle test</b> - रोगी को कहें कि वह अपनी पूरी क्षमता से गहरी सांस लेकर उसे जोर से फूंकते हुए 15cm दूर रखी मोमबत्ती को बुझाये। सामान्य व्यक्ति ऐसा सरलता से कर सकता है परन्तु obstructive airway disease वाले व्यक्ति ऐसा नहीं कर पाते।</p>

