									<p class="mb-2"><b>Spread</b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> T.B का एक मरीज यदि वर्ष भर इलाज रहित रह जाए तो 10 से 12 व्यक्तियों को संक्रमित कर सकता है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> TB का मरीज खांसते छींकते और बात करते वक्त इस बीमारी को सूक्ष्म aerosols के द्वारा फैलाता है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> कमजोर प्रतिकारक शक्ति वाले व्यक्ति को यह बीमारी अतिशीघ्र ग्रसित करती है।</p>
									
									<p class="mb-2"><b>Investigations </b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Sputum AFB smear: Smear positive samples की औसतन 8-10 दिन में रिपोर्ट आ जाती है। Smear negative samples रिपोर्ट के लिए लगभग 2- 6 हफ्ते लगते हैं। Pulmonary TB  की मुख्य जांच बलगम से होती है। बलगम ना बनने पर hypertonic saline या bronchoalveolar lavage से निकाला जाता है। इस टेस्ट से बीमारी के पकड़ में आने की संभावना सिर्फ 50 प्रतिशत है और इसमें cases छूटने का बहुत डर होता है। इसलिए जांच 1000 microscopic fields की जांच कर उसके बाद ही negative दर्शाया जाता है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Auramine, Auramine Rhodamine O ये दो विशेष स्टेन led fluorescent सूक्ष्मदर्शी परीक्षण में उपयोग में लाए जाते हैं।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Lowenstein Jensen broth ( egg based solid culture) जैसी जांच में 4 से 8 हफ्तों में रिपोर्ट आती है। इसमें contamination का acceptance level 3-5% है। TB की confirmatory जांच के लिए culture कराना आवश्यक है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> CBNAAT/ XPERT MTB/RIF ASSAY यह एक महत्वपूर्ण जांच है जिसमे Mycobacterium ka rifampicin resistance बड़ी ही आसानी से टेस्ट किया जा सकता है। यह टेस्ट  Real Time PCR का उपयोग कर 2 घंटे (testing time) में रिपोर्ट दे देता है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> TrueNAT टेस्ट quantitative real-time PCR का उपयोग कर M. Tuberculosis को परखती है। इसमें भी reflex test के जरिए rifampicin resistance देखा जा सकता है। इसमें 1 घंटा TB Bacteria के डिटेक्शन के लिए और 1 घंटा rifampicin resistance detection में अर्थात 2 घंटे लगते हैं।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Middlebrook में 3 से 4 हफ्तों में, Liquid Culture अथवा HPLC में यदि smear Positive हो तो 1 से 2 हफ्ते में रिपोर्ट आती है। Smear negative samples रिपोर्ट के लिए लगभग 2- 6 हफ्ते लगते हैं।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Drug Resistance के लिए line probe assay बहुत ही महत्व पूर्ण जांच है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Line Probe Assay:  Drug resistance को परखने के लिए यह जांच करवाते हैं । इसमें अनेकों दवाईयों से resistance एक ही बार में टेस्ट हो जाता है। जैसे Rifampicin, Isoniazid, Fluoroquinolones, second line injectable drugs इत्यादि। यह एक Molecular testing जांच है, 1-3 दिन में इसकी रिपोर्ट आ जाती है ।</p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Rapid TB Identification test एक immunological test है जो की solid या liquid culture growth पर टेस्ट किया जाता है। यह सिर्फ 15 mins में immunochromatography assay के माध्यम से TB bacteria को परख कर बता देता है।</p>

