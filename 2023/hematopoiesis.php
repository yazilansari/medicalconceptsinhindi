									<h5>Hematopoiesis</h5>					
																		
									<p class="mb-3">Hematopoiesis शब्द का अर्थ है blood cells का निर्माण (Greek Hemato=blood; poiesis=निर्माण)</p>
									
									<p class="mb-3">सर्वप्रथम जानते हैं कि यह शरीर में कहां-कहां होता है। Blood की अधिकाँश cells (RBCs, neutrophils, eosinophils, basophils, monocytes एवं platelets) का निर्माण मुख्यतः bone marrow में ही होता है। T एवं B lymphocytes, lymphoid tissues में भी बन सकते हैं।</p>

									<p class="mb-2"><u>Intrauterine life</u></p>									
									<p class="mb-2">1st trimester – yolk sac</p>									
									<p class="mb-2">2nd trimester – मुख्यतः liver में, spleen एवं lymph nodes में भी</p>									
									<p class="mb-3">3rd trimester – bone marrow</p>									

									<p class="mb-2"><u>After birth</u></p>									
									<p class="mb-2">4 to 5 years – सभी bones की bone marrow में</p>		
									<p class="mb-2">5 to 20 years – केवल long bones की bone marrow में</p>		
									<p class="mb-4">After 20 years – membranous bones की bone marrow में एवं humerus एवं tibia के proximal ends में</p>		

									<p class="mb-2">आओ समझते हैं कि bone marrow की cells, किस प्रकार blood cells उत्पन्न करती 
									हैं।</p>		
									<p class="mb-2">कोई भी प्रारंभिक cell जो शरीर की अनेकों दूसरी cells को उत्पन्न करे, stem cell कहलाती है।</p>
									<p class="mb-3"><b>Stem cells</b> – यह वह undifferentiated cells हैं जिनमें division की अपरिमित क्षमता होती है। Divide करके यह अपने ही समान अन्य stem cells का निर्माण करती हैं साथ ही यह दूसरी विशिष्ट cells में differentiation की क्षमता भी रखती हैं। इस प्रकार stem cells के differentiation से शरीर की अनेकों दूसरी cells का निर्माण कराया जा सकता है।</p>
									<p class="mb-3"><b>Hematopoietic stem cells (HSC)</b> वह stem cells हुयीं जो blood cells को उत्पन्न करें।</p>
									<p class="mb-3"><b>Pluripotential hematopoietic stem cells (PHSC)</b> वह HSC हुयीं जिसमें अनेक प्रकार की blood cells बनाने की क्षमता है (pluri – अनेक, potential – क्षमता) अर्थात इन्हीं cells के division से और अधिक PHSC बनती जाएगीं तथा इनके differentiation से अनेक प्रकार की blood cells का निर्माण हो सकता है। Hematopoiesis की प्रक्रिया में यही सबसे आरंभिक cells हैं।</p>
									<p class="mb-3"><b>Committed stem cells (CSC)</b> – जब उपरोक्त PHSC, blood की किसी एक प्रकार की cell को बनाने के लिए differentiate हो जाती हैं, तब उन्हें committed stem cell कहते हैं (committed= निर्दिष्ट; जिसकी दिशा निश्चित हो चुकी हो)। इन cells को <b>progenitor cells</b> भी कहते हैं (progenitor=पूर्वज) क्योंकि इन्हीं से अन्य सभी blood cells का निर्माण होता है। </p>
									<p class="mb-3">किसी culture medium में यह CSCs किसी एक प्रकार की blood cells की बड़ी संख्या (colony) का निर्माण कर सकती हैं। इसी लिए इन्हें <b>colony forming unit (CFU)</b> भी कहते हैं। विभिन्न blood cells बनाने के लिए भिन्न-भिन्न प्रकार की CFUs निम्नलिखित हैं –</p>

									<p class="mb-3"><b>CFU-E</b> - Erythrocytes के लिए</p>									
									<p class="mb-3"><b>CFU-GM</b> - Granulocytes एवं monocytes, दोनों के लिये</p>									
									<p class="mb-3"><b>CFU-M</b> - Megakaryocytes (एवं platelets) के लिये</p>									
									<p class="mb-3">जो stem cells lymphoid tissues में पहुँच कर वहां T एवं B lymphocytes का निर्माण कराती हैं, उन्हें <b>lymphoid stem cells (LSC)</b> कहते हैं।</p>									
									<p class="mb-3">शरीर अपनी आवश्यकतानुसार CSCs का replication घटा अथवा बढ़ा भी सकता है। जो substances CSCs का replication बढ़ाने में मदद करते हैं उन्हें <b>growth inducers</b> कहते हैं। इनमें एक प्रमुख inducer है, interleukin 3 (IL 3) ।</p>	
									<p class="mb-3">कुछ substances CSCs का differentiation भी बढ़ाने में मदद करते हैं।  इन्हें <b>differentiation inducers</b> कहते हैं।</p>	
									<p class="mb-3">किसी blood cell की synthesis की आवश्यकता कब पड़ेगी, यह शरीर की आवश्यकताओं पर निर्भर करता है।  जैसे, hypoxia की स्थिति मैं RBC synthesis तथा infection की स्थिति में WBCs को बढ़ा दिया जाता है।</p>							
