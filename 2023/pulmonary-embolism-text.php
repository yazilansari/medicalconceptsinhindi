									<h5>Pulmonary Embolism(PE)</h5>
									<p class="mb-4"><b>डॉ श्वेता शर्मा सह-आचार्या मेडिसिन विभाग ,डॉ रचना सेमवाल, सीनियर रेजिडेंट मेडिसिन 
									विभाग</b> </p>														
									<p class="mb-3">लाला लाजपतराय स्मारक वैदिकीय महाविद्यालय, मेरठ</p>

									<p class="mb-3"><b>Introduction-:</b></p>									
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> जब कोई deep venous thrombi अपने स्थान से अलग होकर vena cava के रास्ते होते हुए right atrium व फिर right ventricle में होते हुए, pulmonary artery में embolize होता है, और pulmonary circulation में टिक जाता है तो उसे pulmonary embolism कहते हैं। </p> 
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> लगभग 25% autopsies (शव परीक्षणों) में microvascular एवं macrovascular pulmonary embolism दोनों ही मिले हैं। यह pulmonary embolism बढ़े हुए inflammation, cytokine storm, platelet activation, stasis व endothelial dysfunction के कारण होता है।</p> 
									

									<p class="mb-3"><b>Pathophysiology:-</b></p>									
									<p class="mb-3 dotedt2"><span>1.</span><b>Inflammation:-</b></p>									
									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> यह स्थिति Pulmonary Embolism की शुरुवात में मुख्य भूमिका निभाता है। उदाहरण निम्न हैं -pneumonia, ulcerative colitis, crohn's disease, rheumatoid arthritis, diabetes mellitus type 2, ACS, stroke, धूम्रपान, sepsis, कैंसर, blood transfusion इत्यादि। </p>

									<p class="mb-3 dotedt2"><span>2.</span> <b>Prothrombotic States-</b></p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Factor V (proaccelerin) के Leiden mutation व prothrombin gene के mutation यह सबसे मुख्य परिस्थितियों में से हैं। </p>

									<p class="mb-3 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Antithrombin, protein C व protein S जो की प्राकृतिक रूप से रक्त को जमा नहीं होने देते (anticoagulants), इनकी कमी के कारण भी Venous Thromboembolism हो सकता है ।</p>

									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i>  Antiphospholipid syndrome जो की एक acquired thrombophilic विकार है। इसमें artery व vein दोनों में ही थक्का बनने के संयोग बढ़ जाते हैं। </p>

									

									<p class="mb-3"><b>Clinical Risk Factors</b>-:</p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> सामान्य परिस्थितियां जो Pulmonary Embolism का जोखिम बढ़ाती हैं वे निम्नलिखित है- </p>

									<p class="mb-4">Cancer, obesity, hypertension, COPD, chronic kidney disease, air pollution, long-haul air travel, OCPs, postmenopausal hormone replacement therapy, surgery व  trauma. सुस्त दिनचर्या भी इसका जोखिम बढाती हैं। </p>

									<p class="mb-3"><b>Activated Platelets</b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Virchow’s Triad: यह एक coagulative परिस्थिति है। Venous stasis, hypercoagulability व endothelial injury मिलके यह triad बनाते हैं।  </p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इनके कारण प्लेटलेट्स सक्रिय होती हैं व microparticles को उन्मुक्त करती हैं। ये microparticles से फिर proinflammatory कण निकलते हैं, जो की neutrophils के साथ मिलकर platelets को इक्कठा करवाती हैं व thrombin का उत्पादन करती हैं। जिसके परिणामस्वरुप venous thrombi बनते हैं और विकसित होते हैं। </p>
									

									<p class="mb-3"><b>Physiology</b>-:</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pulmonary Embolism की परिस्थिति में  श्वसन  में मिलने वाली सबसे सामान्य विषमता arterial hypoxemia है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> ऐसेमें Anatomical व  physiological dead space दोनों की ही बढ़ोतरी होती है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इस परिस्थिति में pulmonary vascular resistance भी बढ़ जाता है। साथ ही pulmonary compliance कम हो जाती है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i>  जिसकी वजह से pulmonary artery pressure बढ़ता है जिस कारण right ventricle की दीवार में तनाव बढ़ता है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> अंततः  Right Ventricle dilatation और dysfunction हो जाता है। दीवार में खिंचाव के कारण cardiac biomarker (brain natriuretic peptide) release होते हैं। </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Right Ventricle की दीवार में बढ़ा हुआ तनाव right coronary artery को दबाता है और  myocardium में  ऑक्सीजन की सुलभता को कम कर देता है। जिससे Right Ventricle में microinfarction होने के कारण cardiac biomarker troponin release होता है। </p>


									<p class="mb-2"><b>Classification of Pulmonary Embolism</b>-:</p>
									<p class="mb-2 dotedt2"><span>1.</span> <b>Massive Pulmonary Embolism (High-risk)</b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i>  यह Pulmonary Embolism के 5-10% मामलों में पाया जाता है। इसकी विशेषताएं हैं -systemic arterial hypotension व pulmonary vasculature के कम से कम आधे हिस्से में थक्के का जमना । </p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसके लक्षण हैं- सांस फूलना, चक्कर आना, रक्तचाप कम होना व शरीर नीला पड़ना, cardiogenic shock, multiorgan failure.</p>
									
									<p class="mb-2 dotedt2"><span>2.</span> <b>Submassive Pulmonary Embolism (intermediate-risk)- </b></p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pulmonary Embolism के 20-25% मामलों में मध्यवर्ती जोखिम होता है। इसकी विशेष्ता है RV dysfunction, हालांकि, इसमें रक्तचाप सामान्य रहता है।</p>
									

									<p class="mb-2 dotedt2"><span>3.</span> <b>Low-risk PE</b>-; </p>
									<p class="mb-4 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> ये 65-75% मामलों का गठन करते हैं। और इन मरीज़ों का prognosis उत्कृष्ट होता है। </p>

									<p class="mb-2"><b>Diagnosis</b>-:</p>
									<p class="mb-2"><b>Clinical evaluation</b>-:</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pulmonary Embolism को "the Great Masquerader" के नाम से भी जाना जाता है। क्योंकि इसके लक्षण गैर विशिष्ट होते हैं। इसलिए इसका diagnosis कर पाना मुश्किल होता है।</p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> इसलिए अगर थोड़ी सी भी शंका हो, कि मरीज को Pulmonary Embolism हो सकता है, तो त्वरित रक्त में D-dimer के स्तर को देखना चाहिए। इसकी सामान्य ऊपरी सीमा 500 ng/ml होती है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> हालांकि कई नए दिशा निर्देश यह सलाह देते हैं की Pulmonary Embolism के diagnosis  को खारिज करने के लिए age-adjusted D-dimer का प्रयोग करना चाहिए।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> ये age-adjusted D-dimer उन मरीजों पर लागू होता है, जिनकी आयु 50 वर्ष से ऊपर है। इनमें ऊपरी सामान्य सीमा का आंकलन करने के लिए आयु को 10 से गुणा करना है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Pulmonary Embolism का सबसे आम लक्षण सांस फूलना है। इसलिए जब अप्रत्यक्ष Pulmonary Embolism, pneumonia या congestive heart failure के साथ होता है, तो मरीज में उचित इलाज के बाद भी सुधार नहीं दिख पाता है। ऐसे में हमें Pulmonary Embolism के बारे में सोचना चाहिए।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Well Point Score criteria: ये criteria, Pulmonary Embolism की clinical diagnosis और  आंकलन करने में मदद करता है। इसके अनुसार, </p>
									<p class="mb-4">§  यदि मरीज में Pulmonary Embolism होने की निम्न से मध्यम संभावना है तो, पहले d-dimer के स्तर देखने चाहिए। यदि, इसका स्तर सामान्य है, तो हमें इसके अतिरिक्त कोई परीक्षण करने की आवश्यकता नहीं है। </p>
									<p class="mb-4">§  इसके विपरीत जब संभावना ज्यादा हो तो हमें अतिशीघ्र मरीज की imaging करवानी चाहिए।</p>

									<p class="mb-3"><b>Non-Imaging Diagnostic Modalities:</b></p>
									<p class="mb-2"><b>Blood Test</b>:</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> D dimer ELISA- Pulmonary Embolism के मरीजों में plasmin द्वारा fibrin के टूटने से बढ़ जाता है।Pulmonary Embolism  में  D-dimer की sensitivity 95% से अधिक है। इसलिए d-dimer का सामान्य स्तर Pulmonary Embolism के diagnosis  को खारिज करने में सहायक है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> परन्तु, रक्त में d-dimer का स्तर, pulmonary embolism के लिए specificity नही रखता है। अर्थात, इसके स्तर बहुत सी परिस्थितियों में बढ़ सकते हैं जैसे कि - myocardial infarction, pneumonia, sepsis, cancer, postoperative state, pregnancy के 2nd और 3rd trimester में। </p>
									
									<p class="mb-3"><b>Cardiac biomarkers</b></p>
									<p class="mb-2"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> खून में troponin और heart type fatty acid binding protein, brain natriuretic peptide भी बढ़ा हुआ मिल सकता है।</p>
									


									<p class="mb-2"><b>Electrocardiogram</b> </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Sinus tachycardia सबसे ज्यादा मिलने वाली विषमता है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> इसके अलावा S1Q3T3 sign भी मिल सकता है। इसमें lead I में S wave, lead III में Q wave और inverted T wave मिलती है। ये लक्षण तुलनात्मक रूप से specific है, पर sensitive नहीं है।</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Right Ventricle की दीवार में तनाव व ऑक्सीजन की कमी के कारण V1-V4 में T wave inversion भी सामान्य तौर पर मिल जाता है।</p>

									
									<p class="mb-3"><b>Non-invasive Imaging Modalities:</b></p>
									<p class="mb-3"><b>Chest Roentgenography</b>-:</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> छाती के एक्सरे में अक्सर कोई संकेत नहीं मिलते है। कुछ विशिष्ठ असामान्यता मिल सकती हैं, जैसे </p>

									<p class="mb-2 dotedt"><i class="fa fa-angle-double-right" aria-hidden="true"></i> Westermark's sign: जो की focal oligemia होता है। </p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Hampton's Hump: pleura की परिधि में स्थित एक wedge-shaped density</p>

									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Palla’s sign: बढ़ी हुई दाईं descending pulmonary artery </p>
									
									<p class="mb-2"><b>Chest CT-</b> </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> CT scan (CE-CT): Contrast Dye के साथ किया गया छाती का CT scan, Pulmonary Embolism की diagnosis  के लिए महत्वपूर्ण जांच है। </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> <b>Invasive contrast pulmonary angiography:</b> इससे बेहतर resolution दिखता है। यह heart के chambers को भी अच्छे से देखने में मदद करता है। इसमें अगर Right Ventricle का आकार सामान्य की अपेक्षा बढ़ा हुआ मिलता है, तो ऐसे मरीज़ों में अगले 30 दिनों में मृत्यु की संभावना ज्यादा होती है ।  CTPA- Computed tomography-Pulmonary Angiography यह जांच pulmonary  embolism की gold standard जाँच है।  </p>

									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> 
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Pulmonary Embolism के लिए यह अब 2nd-line diagnostic test है। यह जाँच ऐसे मरीजों में की  जाती है, जो I.V Contrast dye नहीं बर्दाश्त कर पाते हैं।</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Perfusion scan में दिक्कत आने का मतलब है खून का प्रवाह या तो कम हो गया है या पूरी तरह से समाप्त हो गया है और यह Pulmonary Embolism की ओर संकेत करता है ।</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Ventilation scan में abnormality आना non ventilated lung की ओर संकेत करती है। यह Pulmonary Embolism के अलावा भी, जिन बीमारियों में perfusion defect मिलता है उनकी ओर इशारा करता है। जैसे की Asthma  एवं COPD</p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> अगर scan में दो या उससे ज्यादा perfusion defect मिलते हैं, पर वेंटिलेशन सामान्य है, तो Pulmonary Embolism की संभावना सर्वाधिक है। इसकी संभावना बहुत ही कम हो जाती है, जब scan पूरी तरह से सामान्य है।</p>

									<p class="mb-2"><b>Echocardiography -</b> </p>									
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Pulmonary Embolism के अधिकतर मरीजों में Echo सामान्य होता है, इसलिए इसके diagnosis के लिए 2D Echo विश्वसनीय जांच नहीं है। </p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> McConnell's Sign: यह sign Pulmonary Embolism की स्थिति में, Transthoracic echocardiography में मिलने वाला अप्रत्यक्ष sign है। इसमें Right Ventricle के free wall का hypokinesia तथा apex का hyperkinetic या सामान्य motion मिलता है।</p>

									<p class="mb-2"><b>Invasive Diagnostic Modalities:</b> </p>									
									<p class="mb-2"><b>Pulmonary Angiography-: </b> </p>									
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> यह जांच ऐसे मरीजों के लिए रिजर्व की गई है जिनमें CECT का परिणाम संतोषजनक न आया हो या फिर जिन मरीजों में interventional procedure जैसे catheter-directed thrombolysis करने का प्लान हो।</p>
									<p class="mt-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Pulmonary Embolism का स्पष्ट diagnosis  तभी बनाया जा सकता है जब हमें एक से ज्यादा intraluminal filling defect मिलें। </p>


									<p class="mb-2"><b>Risk Stratification:-</b> </p>											
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> इलाज़ शुरू करने से पहले हमें जोखिम के स्तर का अनुमान लगाना जरूरी है। </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Hemodynamic अस्थिरता, Right Ventricle का निष्क्रिय होना, छाती के CT scan पर बढ़ा हुआ Right Ventricle का मिलना व troponin के स्तर का बढ़ना सब बढ़े हुए जोखिम की ओर संकेत करते हैं। </p>
									

									<p class="mb-4"><b>Anti Coagulant -</b></p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> असरदार anticoagulation, Pulmonary Embolism के सफल इलाज के लिए अत्यंत आवश्यक है। इसके लिए तीन प्रमुख रणनीतियां हैं। </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> Unfractionated heparin (UFH), LMWH या फिर fondaparinux के से Parenteral anticoagulation प्रदान करने के बाद warfarin देना- यह बहुत ही प्रतिष्ठित रणनीति है, परन्तु अब समय के साथ इसका प्रयोग कम हो गया है।  </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> पांच दिन तक parenteral anticoagulation देने के बाद, नए oral anticoagulant जैसे की dabigatran (direct thrombin inhibitor) या edoxaban (Anti-Xa agent) पर switch  कर सकते हैं। </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> सीधे oral anticoagulant जैसे की rivaroxaban और apixaban का प्रयोग कर सकते हैं। यह दोनों ही Anti-Xa agents हैं। Rivaroxaban को तीन हफ्ते व apixaban को एक हफ्ते तक loading dose में देना है, जिसके बाद maintenance dose दे सकते हैं। </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> अगर मरीज़ को heparin induced thrombocytopenia हो गयी है या उसके होने का संदेह है, तो argatroban या bivalirudin (parenteral direct thrombin inhibitors) का उपयोग करना चाहिए। </p>
									

									<p class="mb-3 dotedt2"><span>1.</span> Unfractionated heparin - UFH, antithrombin से जुड़कर उसकी सक्रियता को बढ़ा देता है। और इसलिए आगे thrombus बनने से रोकता है। </p>
									<p class="mb-2 dotedt2"><span>2.</span> Low-Molecular-Weight Heparin- </p>
									<p class="mb-2 dotedt2"><span>a.</span> LMWH की plasma protein से व endothelial cells से जुड़ने की क्षमता कम होती है। इलसिए इसकी bioavailability और half-life ज्यादा होती है।  </p>
									<p class="mb-4 dotedt2"><span>b.</span> इसकी प्रतिक्रिया भी उम्मीद के अनुसार होती है। साथ ही इसका प्रयोग करते समय ज्यादा निगरानी या डोज़ में बदलाव करने की भी आवश्यकता नहीं पड़ती हैं।</p>
									
									<p class="mb-2 dotedt2"><span>3.</span><b>Fondaparinux</b></p>
									<p class="mb-2"> यह एक anti-Xa pentasaccharide  व अल्ट्रा-LMWH है। </p>
									<p class="mb-4 dotedt2"><span>a.</span> यह मरीज़ के वजन के अनुसार दिया जता है। हमें लैब की किसी भी जाँच पे निगरानी रखने की आवश्यकता नहीं पड़ती है। </p>
									
									<p class="mb-2 dotedt2"><span>4.</span> Warfarin </p>
									<p class="mb-2 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i>  यह एक विटामिन K antagonist है और ये Factors II,VII,IX,X को सक्रिय होने से रोकता है।  </p>
									<p class="mb-3 dotedt2"><span>a.</span> यह एक विटामिन K antagonist है और ये Factors II,VII,IX,X को सक्रिय होने से रोकता है।  </p>
									<p class="mb-3 dotedt2"><span>b.</span> इस दौरान निर्धारित लक्ष्य INR को २-३ के बीच रखना होना चाहिए। </p>
									<p class="mb-4">इसकी शुरुआत 5 mg से करनी चाहिए। </p>
									
									<p class="mb-2 dotedt2"><span>5.</span> Novel Oral Anticoagulant- </p>
									<p class="mb-3 dotedt2"><span>a.</span> NOACs को एक निश्चित डोज़ में दिया जाता है और यह खाने के कुछ घंटे बाद ही असरदार anticoagulation प्रदान करना शुरू कर देते हैं। </p>
									<p class="mb-3 dotedt2"><span>b.</span> हमें बार-बार INR जांच करने की आवश्यकता नहीं पड़ती है व साथ ही इनको दूसरी दवाइयों के साथ प्रयोग करके कोई परस्पर असर नहीं पड़ता है। </p>
									<p class="mb-3 dotedt2"><span>c.</span> इनके साथ हरी सब्जियां खाने पर कोई रोक नहीं है।</p>
									<p class="mb-3 dotedt2"><span>d.</span> Rivaroxaban को शुरुवात के तीन हफ़्तों तक 15mg दिन में दो बार देना है व उसके बाद 20mg दिन में एक बार रात के खाने से पहले। </p>
									<p class="mb-3 dotedt2"><span>e.</span> Apixaban को 10 mg दिन में दो बार एक हफ्ते के लिए उसके बाद दिन में दो बार 5 mg</p>
									<p class="mb-3 dotedt2"><span>f.</span> Dabigatran को शुरू करने से पहले पांच दिन तक parenteral anticoagulants में से कोई एक देना है। फिर dabigatran 150 mg दिन में दो बार शुरू करना है।</p>
									<p class="mb-3 dotedt2"><span>g.</span> इसी तरह edoxaban को शुरू करने से पहले भी पांच दिन parenteral anticoagulant देना है।  फिर edoxaban को दिन में एक बार 60 mg देना है। </p>
									

									<p class="mb-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i> ऐसे मरीज़ों में अगर रक्तचाप गिर रहा है तो हमें intravascular volume की पूर्ति करने के लिए 500 ml NS तुरंत देना है। इससे ज्यादा फ्लूइड देने में हमें काफी सावधानी बरतनी पड़ेगी क्यूंकि शरीर में ज्यादा volume बढ़ने से RV की दीवारों पर तनाव बड़ सकता है जिसके चलते LV की compliance और ख़राब हो सकती है व मरीज़ hemodynamicallly और अस्थिर हो सकता है। </p>
									<p class="mb-3 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i>अगर मरीज़ शॉक में है तो इसके 1st line vasopressors हैं norepinephrine व dobutamine दें।  </p>
									<p class="mb-4 dotedt"><i class="fa fa-long-arrow-right" aria-hidden="true"></i>ज्यादा गंभीर परिस्थितियों में हम ECMO(extracorporeal membrane oxygenation)का प्रयोग कर सकते हैं। इसे एक ब्रिज की तरह ही उपयोग करना चाहिए जिस दौरान हमें साथ साथ  thrombolysis या embolectomy जो की definitive treatment हैं। </p>				

