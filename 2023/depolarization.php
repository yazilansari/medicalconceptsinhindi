									<h5>Depolarization - कार्यारंभ</h5>	
									<!-- <p>Spirometry - Method And Interpretation</p> -->
									
									<!-- <p class="mb-2"><b>Lung Function Tests (Part 1)</b></p> -->
									<p class="mb-3">किसी neuron में Na+K+ pump, जब तक Na+ को बाहर एवं K+ को भीतर pump करता रहेगा, तब तक वह neuron resting phase में ही रहेगा. कार्य आरम्भ करने हेतु तो इनको पुनः अपनी पूर्वावस्था में लाना होगा न? यह स्थिति किसी railway crossing के समान हो गई जहाँ सड़क के बाईं ओर का gate बंद होने से सारा traffic gate के एक तरफ़ एकत्रित हो गया एवं दाईं ओर का gate बंद होने से railway line के दूसरी ओर. अब जैसे ही यह gate खुलेगा, traffic का rush एकाएक railway line को पार करना चाहेगा. </p>

									<!-- <p class="mb-2">Clinical parameters</p>									 -->
									<p class="mb-2">किसी neuron में कोई external stimulus, इन gates को खोलने का कार्य करता है. जिस प्रकार कुछ railway lines पर दोनों gates एक साथ न खोलकर एक-एक करके खोले जाते हैं, उसी प्रकार यह stimulus भी Na+ channels को पहले खोलता है. याद करो, किस प्रकार रेल की जनरल बोगी में एक दरवाज़ा थोड़ा-सा खोलते ही, बाहर भीड़ का रेला धकेलकर उसे पूरा खोल देता है तथा तुरंत ही कुछ लोग अंदर घुसकर चारों दरवाज़े खोल देते हैं. इससे यह रेला चारों ओर से एकदम बोगी में भरने लग जाता है. ठीक इसी प्रकार, कुछ Na+ channels के खुलते ही, Na+ भी neuron के भीतर प्रवेश करने लगते हैं. यह Na+, neuron के interior में पहले से ही उपस्थित negative charge (-90mV) को positive की ओर ले जाना प्रारंभ कर देते हैं. Intraneuronal potential के -65mV के आसपास (-70 से -50mV के मध्य) पहुँचते-पहुँचते बहुत बड़ी संख्या में Na+ channels खुल जाते हैं जिससे एकाएक Na+ की बहुत बड़ी संख्या neuron में प्रवेश कर जाती है. जहाँ -90 से -65mV पहुँचने में कुछ समय लगता भी है, वहीं -65 से 0 mV तक यह उससे काफ़ी कम समय में पहुँच जाता है. जिस प्रकार भीड़ का रेला अनियंत्रित हो रेल की बोगी को उसकी क्षमता से अधिक भर देता है, उसी प्रकार यह Na+ influx भी intraneuronal potential को 0 mV पर ही न रोककर क्षणिक तौर पर electroposive भी बना देता है। क्या तुम्हें याद है कि Na+ का equilibrium potential कितना होता है? इसका अर्थ हुआ कि यदि यह Na+ channels खुले रहें तब Na+influx, neuron के intraneural potential को +60 mV (Na+ का equilibrium potential) तक पहुंचा देगा। ऐसे में प्रश्न यह उठता है कि कोई AP इसे +35 mV तक ही क्यों पहुंचा पाता है? वास्तव में, किसी AP के समय यह Na+channels केवल कुछ ही देर के लिए खुलते हैं जिससे Na+influx बीच में हो रुक जाता है तथा intraneural potential, Na+ के equilibrium potential +60 mV तक न पहुँच कर, बीच में +35 mV पर ही रुक जाता है। इसके अतिरिक्त, Na+ channels के बंद होने के साथ-साथ ही K+ channels भी खुलने लग जाते हैं, जो intraneural potential को दोबारा electronegativity की ओर ले जाना प्रारम्भ कर देते हैं।</p>
																	
									<p class="mb-4"> इस प्रकार, Na+ का यह massive influx ही membrane potential में बड़ा बदलाव उत्पन्न कर एक current को उत्पन्न करता है जो उस nerve cell को action में ला सके. इसीलिये इसे action potential (AP) कहते हैं.</p>
									
