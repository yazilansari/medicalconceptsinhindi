									<h5>Transport of sunstances across cell membrane</h5>										
																		
									<p class="mb-2">तुम यह तो जान ही चुके हो कि lipid soluble substances cell membrane की lipid bilayer से होकर इतनी सहजता से गुजर जाते हैं जैसे कि इस membrane का कोई अस्तित्व ही न हो, परन्तु water soluble substances इससे होकर नहीं निकल पाते। इन्हीं water soluble substances के लिये lipid bilayer के मध्य अनेकों integral proteins के globular molecules होते हैं, जिनके दोनों छोर cell membrane के दोनों ओर निकले रहते हैं। इस प्रकार यह molecule ECF एवं ICF दोनों के सम्पर्क में रहते हैं। क्योंकि यह protein channels किसी cell membrane को बाहर से भीतर तक इसकी पूर्ण thickness में cover करती हैं, इसीलिये इन्हें membrane spanning proteins भी कहते हैं। इनमें से कुछ protein molecules, इन water soluble substances के आवागमन के लिये मार्ग (channel) भी बनाते हैं। जिस प्रकार कई महत्वपूर्ण रास्तों पर ‘rights of admission, reserved’ रहता है उसी प्रकार यह channels भी अपने से होकर कुछ विशिष्ट पदार्थों को ही गुजरने देते हैं, सभी को नहीं। इसे यूं भी कह सकते हैं कि विभिन्न पदार्थों के लिये cell membrane में भिन्न-भिन्न channels होते हैं। किसी channel के pore का internal diameter, उसकी shape एवं इसके अन्दर उपस्थित electrical charges इस बात का निर्धारण करते हैं कि channel से गुजरने वाला पदार्थ किस प्रकार का होगा।</p>
									
									<p class="mb-2">अब यदि cell के बाहर से कोई सूचना cell के भीतर पहुंचानी हो तब इसकी व्यवस्था क्या होगी? वास्तव में यह integral protein एक messenger का कार्य भी करती हैं। इसके लिये cell membrane की बाहरी surface पर इन integral proteins से एक अन्य peripheral protein जुड़ी रहती है, जो उस information को लाने वाले ligand (जो आकर receptor से जुड़ता हो) के लिये receptor का कार्य करती है। Ligand के इस receptor से जुड़ने पर ligand द्वारा लायी गयी information cell के भीतर पहुंचा दी जाती है, जहां यह या तो सीधे-सीधे स्वयं कार्य कर देती है। (जैसे किसी enzyme का activation या किसी channel का खोलना अथवा बंद करना) अथवा किसी दूसरे messenger (second messenger) द्वारा यह information आगे pass on कर दी जाती है।</p>

