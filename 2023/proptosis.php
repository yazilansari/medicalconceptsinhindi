									<h5>Proptosis</h5>
									<p class="mb-3">Dr Pankaj Kumar Agarwal</p>								
									<p class="mb-3">सर्वप्रथम ध्यान दो कि इस चित्र में तुम्हें क्या विशेष लक्षण दिखाई दे रहे हैं? 1. Upper eyelids का cornea के upper part को cover न करना, जिससे इसके ऊपर की sclera दिख रही है। 2. इससे दोनों eyelids के बीच का palpebral fissure का wide हो जाना। </p>

									<p class="mb-3">क्या तुमने कभी ध्यान दिया कि eyelids एवं cornea में क्या संबंध रहता है? वास्तव में upper eyelid cornea के 1-2 mm भाग (लगभग upper 1/5th) को cover रखती है, जबकि lower eyelid, lower corneo-scleral junction तक पहुंचती है। यदि यह eyelids अपनी सामान्य स्थिति तक नहीं पहुंच पाती या यों कहें कि अपनी सामान्य स्थिति से वापिस खींच ली जाती है (retraction of eyelids) तब अधिकांशतया upper sclera एवं severe cases में lower sclera भी दिखलायी पड़ने लगती है। इससे दोनों eyelids के बीच का खुला भाग - palpebral fissure, wide हो जाता है एवं ऐसा प्रतीत होता है जैसे, eyeballs बाहर को निकल रही हों, यहां तक कि severe cases में वह बाहर को गिर पड़ेगी। इसे proptosis कहते हैं, जिसका अर्थ है, Latin pro अर्थात forward एवं piptein अर्थात fall।  </p>

									<p>याद करो upper eyelid का eyeballs को cover करना एवं खोलना, यह movements किस muscles द्वारा कराये जाते हैं? यदि eyeball को ढंकना हो तब orbicularis occuli muscle contract करती है, जो किसी sphincteric muscle की भांति दोनों eyelids के मध्य का gap कम कर के eyeballs को पूरा ढंक सकती है। यह movement upper एवं lower eyelids में एक साथ होता है। जब तुम अपनी आंखों को कस कर भींचते हो तब तुम इस muscle के function को ठीक प्रकार से प्रतीत कर सकते हो। परन्तु यह इस muscle के orbital part से होता है। सोते समय या eyes के blink करते समय eyes का gentle closure, इस muscle के palpebral part से होता है। इसके विपरीत eyeballs को खोलने के लिये upper eyelid को ऊपर उठाना होगा। इसकी muscle है, <b>levator pepebrae superioris (LPS)</b>। याद करो, LPS, sphenoid bone के lesser wing की orbital surface से originate हो कर आगे upper eyelid तक पहुंचती है, जिससे contraction के समय इसे ऊपर उठा सके। Insertion के पूर्व यह दो भागों में बंट जाती है। Superior slip, upper eyelid की skin में orbicularis occuli के fibres के बीच-बीच में insert होती है तथा inferior slip, tarsal plate के superior part में। Proptosis में इस inferior slip एवं tarsal plate का बहुत बड़ा role है।  </p>


									<p>परन्तु क्या तुम्हें tarsal plate ठीक-ठीक याद है? Face के deep fascia को ही eyelids में palpebral fascia कहते हैं। क्योंकि यह fascia eyes को cover करने में प्रयुक्त होता है, अतः इसे orbital septum भी कहते हैं। यही orbital septum, lid margin के समीप पहुंचकर thick हो जाता है। तथा upper एवं lower eyelids में क्रमशः upper एवं lower tarsal plates बनाता है। यह tarsal plates, eyelid margins को दृढ़ता प्रदान करती हैं एवं उन्हें एक साथ एक cover के रुप में खुलने तथा बंद होने में मदद करती हैं। </p>

									<p>अब सोचो इन muscles के dysfunction से यह proptosis किस प्रकार उत्पन्न हुई? Proptosis का प्रमुख लक्षण है, palpebral fissure का wide हो जाना, जो प्रमुखतः upper eyelid के retraction से होता है, जो पुनः LPS की hyperactivity से होता है। परन्तु यह LPS muscle, hyperactive किस प्रकार से हुई? क्या तुम्हें इसका innervation ध्यान है? वास्तव में LPS की inferior slip के tarsal plate पर insert होने वाले fibres, smooth muscles के होते हैं, जिन्हें सम्मिलित रुप से <b>Muller's muscle</b> कहते हैं। अब smooth muscle किसी somatic nerve से innervation तो प्राप्त करेगी नहीं। अतः इस के लिये sympathetic fibres इसे paravertebral sympathetic chain से मिलते हैं, जो superior cervical sympathetic ganglion से internal carotid nerve के रूप में निकलने के बाद, internal carotid artery के चारों ओर लिपटे हुएए cranial cavity में पंहुचते हैं। Cavernous sinus में यह fibres, occulomotor nerve (IIIrd nerve) में pass on हो जाते हैं, जिसकी superior branch से होते हुए यह Muller muscle तक पहुंचते हैं। </p>
									
									<p>याद करो, sympathetic stimulation का क्या कार्य है? यह हमें किसी संकट के लिए तैयार करती है। अतः यह आंखों को पूरी तरह खोलने में मदद करेगी। इस प्रकार, thyrotoxicosis में हुआ sympathetic stimulation, Muller muscles को contract करा कर upper eyelid का retraction एवम् palpebral fissure की widening करा देता है। </p>

									<p class="mb-2">अब अगला प्रश्न यह उठता है कि, thyrotoxicosis का sympathetic stimulation से क्या सम्बन्ध? वास्तव में, thyroid hormone excess में epinephrine एवं norepinephrine का secretion अथवा plasma level नहीं बढ़ता, वरन् body tissues की इन hormones के लिये sensitivity बढ़ जाती है। इस प्रकार thyrotoxicosis में Muller's muscles की sensitivity भी इन hormones के लिये बढ़ जाती है तथा वह आवश्यकता से अधिक contract कर proptosis उत्पन्न कर देती है।</p>									
