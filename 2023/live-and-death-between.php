									<h5>जीवन, मृत्यु एवं इनके बीच Physiology</h5>	
									<!-- <p>Spirometry - Method And Interpretation</p> -->
																		
									<p class="mb-2">जीवन क्या है? स्पष्ट परिभाषा कोई नही जानता, परंतु शायद वह प्रक्रियाएं जो living organisms को nonliving से पृथक करती है, जीवन की परिचायक हैं। यह प्रक्रियाएं क्या हैं? Growth एवं reproduction सभी living organisms में दिखलायी पड़ती हैं। मनुष्यों में यह हृदय के धड़कने, सांस लेने, हाथ पैर चलाने, इत्यादि गतिविधियों से प्रकट होती हैं। मृत्यु क्या है? इसकी कोई स्पष्ट परिभाषा नही। कहा जाता है कि जब जीवन नही रहा, वही मृत्यु है। तब क्या यह मानें कि जब यह शारीरिक प्रक्रियाएं बंद हो जायें तब वह मृत्यु कहलायेगी शायद यही सत्य है। अंत में घूम फिर कर हम पुनः इन्हीं शारीरिक प्रक्रियाओं पर ही आ गये। अर्थात जीवन को समझने के लिये हमें इन प्रक्रियाओं को समझना होगा और इन्हीं शारीरिक प्रक्रियाओं के अध्ययन का नाम है, physiology.</p>
									
									<p class="mb-3"> मानव शरीर में हम जीवन को किस प्रकार पहचानते हैं? Growth एवं reproduction देखने में वर्षों लग जाते हैं। Digesion, excretion एवं locomotion भी कई-कई घंटों तक होते हुए भी दिखलायी नही पड़ते। Heart | beat एवं respiration ही वह दो प्रक्रियाएं हैं जो हर समय चलती हुई प्रतीत होती हैं। इसीलिये वर्षों से इन दो प्रक्रियाओं के रुक जाने को मृत्यु का द्योतक मान लिया जाता है तथा death certificate पर पूर्णरुपेण उचित न होने पर भी मृत्यु का लक्षण एवम् कारण cardio respiratory arrest लिख दिया जाता है। परंतु क्या यह पूर्ण सत्य है? यदि हम किसी मरणासन्न व्यक्ति को ventilator पर रख दें तब क्या उसका respiration एवं उसकी heart beat इस life support पर काफी लंबे समय खींची नही जा सकती? क्या तुम इसे जीवन मानोगे? इसीलिये कुछ समय पूर्व brain death के concept का आरंभ हुआ जो technically अधिक उचित प्रतीत होता है। इसके अनुसार यदि brain के विभिन्न भागों ने कार्य करना बंद कर दिया है तब इसे मृत्यु का लक्षण मानना अधिक उपयुक्त होगा। क्या इस विचार ने और अधिक दुविधाओं को जन्म नही दिया है। अब यह कैसे माना जाये कि brain कार्य कर रहा है अथवा नहीं? जागते समय brain के क्रिया कलाप प्रकट हैं, परन्तु सोते समय नही। परंतु यह मृत्यु तो नहीं। Semiconsciousness, unconsciousness एवं यहां तक कि coma तक में brain functions दिखलायी नही पड़ते, परंतु यह भी मृत्यु नही। Brain के विभिन्न भागों में चल रही अंदरुनी गतिविधियों को electroencephalogram (EEG) के द्वारा जाना जा सकता है। यदि यह भी दिखलायी नही देती, तब किसी sensory organ को stimulate कर brain में उसकी प्रतिक्रिया evoked potential के रुप में इसके द्वारा देखी जा सकती है। वास्तव में यह सभी investigations, nervous system के विभिन्न neurons में चल रही गतिविधियों को दर्शाते हैं। अब neurons में होने वाली इन गतिविधियों का आधार क्या है? ध्यान दो, अभी हमने किसी sensory organ के stimulation पर उत्पन्न होने वाले evoked potential की चर्चा की थी। Potential का अर्थ है, किसी barrier के दोनों ओर स्थित electrical charges में अंतर। Neurons के कार्य करने की स्थिति में neuronal membrane के दोनों ओर यह electrical charges move करते हैं तथा membrane potential को परिवर्तित करते रहते हैं। सूक्ष्म रुप में जीवन शायद यही है। तब क्यों न आरंभ यहीं से किया जाये।</p>

