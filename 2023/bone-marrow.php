									<h5>Bone Marrow</h5>					
																		
									<p class="mb-3">Long (cortical) bones की central medulla में एवं cancellous bones में trabeculae के बीच-बीच के स्थान में जो soft tissue भरा रहता है उसे marrow कहते हैं। यह दो प्रकार की हो सकती है, red marrow - जो सक्रिय रूप से blood formation (hemopoiesis) होता रहता है तथा yellow marrow - जिसमें मुख्यतः fat भरा रहता है। शिशु के जन्म के समय cortical एवं cancellous, दोनों प्रकार की marrow में hemopoiesis होती है (यह red marrow होती हैं) जबकि उम्र के साथ इसमें fat के जमा होने से यह धीरे-धीरे yellow marrow में बदलती जाती है। यह yellow marrow भी उम्र के साथ-साथ fibrose होती जाती है। वयस्कों में red marrow केवल long bones के ends पर एवं cancellous bones में ही बची रह पाती है। अलग-अलग bones में बिखरी होने के कारण हम शरीर में marrow की मात्रा का उचित प्रकार से आकलन नहीं कर पाते। वास्तव में शरीर की समस्त marrow मिलकर लगभग 1 किलो से भी अधिक हो सकती है।</p>
									
									<p class="mb-3">Fetal life में bone marrow के अतिरिक्त कुछ और भी organs, hemopoiesis में भाग लेते हैं। ये हैं, liver एवं spleen । जन्म के समय तक इन organs में hemopoiesis बंद हो जाती है किन्तु यदि किसी प्रकार से bone marrow नष्ट हो जाए तब उस परिस्थिति में इन organs में सीमित मात्रा में hemopoiesis दोबारा आरम्भ हो सकती हैं। Medulla में स्थित bone marrow से बाहर होने वाली इस hemopoiesis को extramedullary hemopoiesis कहते हैं।</p>

									<p class="mb-3">Peripheral blood में तुम पाओगे कि RBCs की संख्या WBCs की अपेक्षा 500 गुना तक अधिक होती है। जरा सोचो, bone marrow में RBCs अथवा WBCs series की precursor cells का अनुपात क्या होता होगा? तुम्हें यह जान कर आश्चर्य होगा कि bone marrow में WBC series की precursor cells की संख्या RBCseries की precursor cells की अपेक्षा लगभग 3 गुना अधिक होती हैं। इसका कारण यह है कि RBCs का life span तो लगभग 120 दिन होता है जिससे एक बार बनने के बाद वह काफी समय तक circulation में रहती हैं जबकि WBCs का उससे बहुत कम। इसलिए, short lifespan वाली WBCs को बनाने के लिए, long lifespan वाली RBCs की अपेक्षा, काफी अधिक precirsor cells को काम में लगाना पड़ता है।</p>

									<p class="mb-3">Embryo के blastocysts की कुछ primitive cells शरीर की किसी भी cell को बनाने की क्षमता रखती हैं। इन्हें totipotent stem cells कहते हैं। यही cells, bone marrow में पहुंचकर blood cells बनाने लग जाती हैं। अब इन्हें hematopoietic stem cells कहते हैं। यह किसी भी प्रकार की blood cells को बनाने की क्षमता रखती हैं। जब यही hematopoietic stem cells, किसी एक प्रकार की blood cell को बनाने के लिए committed हो जाती हैं तब इन्हीं को committed stem cells या progenitor cells (progeny=वंशज; अर्थात एक वंश को चलाने वाली) कहते हैं।</p>

									<p class="mb-3">इस प्रकार bone marrow से प्रत्येक प्रकार की blood cell का निर्माण होता है। यहाँ दो प्रश्न उठते हैं। क्या किसी blood cell का निर्माण bone marrow के अतिरिक्त, कहीं और से भी हो सकता है; एवं क्या bone marrow में RBCs, WBCs एवं platelets के अतिरिक्त कोई और भी cells बनती हैं? वास्तव में, lymphocyte series की कुछ progenitor cells, bone marrow से migrate करके secondary lymphoid organs (जैसे lymph nodes, spleen, tonsils एवं Peyer’s patches) में पहुँच जाती हैं जहाँ उनके replication से lymphocytes का निर्माण हो सकता है। साथ ही, bone marrow उपरोक्त cells के अतिरिक्त अनेकों अन्य cells का निर्माण भी करती हैं जैसे, osteoclasts (जो bone turnover में भाग लेती हैं), Kupfer cells, Langerhans cells, dendritic cells एवं mast cells ।</p>									
